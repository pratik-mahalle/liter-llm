fn emit_chat_test(out: &mut String, fixture: &Fixture) {
    let req_json = serde_json::to_string(&fixture.api.request).unwrap_or_default();
    let req_escaped = req_json.replace('\\', "\\\\").replace('`', "\\`").replace("${", "\\${");

    let is_error = fixture.api.mock_response.status >= 400 || !fixture.assertions.expect_success;

    if is_error {
        writeln!(out, "      let threw = false;").unwrap();
        writeln!(out, "      try {{").unwrap();
        writeln!(out, "        await client.chat(JSON.parse(`{req_escaped}`));").unwrap();
        writeln!(out, "      }} catch (e) {{").unwrap();
        writeln!(out, "        threw = true;").unwrap();
        emit_error_assertions(out, fixture);
        writeln!(out, "      }}").unwrap();
        writeln!(
            out,
            r#"      expect(threw, "Expected client.chat to throw").toBe(true);"#
        )
        .unwrap();
    } else {
        writeln!(
            out,
            "      const response = await client.chat(JSON.parse(`{req_escaped}`));"
        )
        .unwrap();
        writeln!(out).unwrap();
        emit_chat_assertions(out, fixture);
    }
}

fn emit_stream_test(out: &mut String, fixture: &Fixture) {
    let req_json = serde_json::to_string(&fixture.api.request).unwrap_or_default();
    let req_escaped = req_json.replace('\\', "\\\\").replace('`', "\\`").replace("${", "\\${");

    let is_error = fixture.api.mock_response.status >= 400 || !fixture.assertions.expect_success;

    if is_error {
        writeln!(out, "      let threw = false;").unwrap();
        writeln!(out, "      try {{").unwrap();
        writeln!(
            out,
            "        const stream = await client.chatStream(JSON.parse(`{req_escaped}`));"
        )
        .unwrap();
        writeln!(out, "        for await (const _chunk of stream) {{ /* drain */ }}").unwrap();
        writeln!(out, "      }} catch (e) {{").unwrap();
        writeln!(out, "        threw = true;").unwrap();
        emit_error_assertions(out, fixture);
        writeln!(out, "      }}").unwrap();
        writeln!(
            out,
            r#"      expect(threw, "Expected client.chatStream to throw").toBe(true);"#
        )
        .unwrap();
        return;
    }

    writeln!(
        out,
        "      const stream = await client.chatStream(JSON.parse(`{req_escaped}`));"
    )
    .unwrap();
    writeln!(out, "      const chunks: unknown[] = [];").unwrap();
    writeln!(out, "      for await (const chunk of stream) {{").unwrap();
    writeln!(out, "        chunks.push(chunk);").unwrap();
    writeln!(out, "      }}").unwrap();
    writeln!(out).unwrap();

    let meaningful_count: usize = fixture
        .api
        .mock_response
        .stream_chunks
        .iter()
        .filter(|c| {
            c.get("choices")
                .and_then(|ch| ch.as_array())
                .and_then(|arr| arr.first())
                .and_then(|ch| ch.get("delta"))
                .and_then(|d| d.get("content"))
                .and_then(|v| v.as_str())
                .is_some_and(|s| !s.is_empty())
        })
        .count();

    let total_count = fixture.api.mock_response.stream_chunks.len();

    if meaningful_count > 0 {
        writeln!(
            out,
            "      expect(chunks.length, \"Expected at least {meaningful_count} chunk(s)\").toBeGreaterThanOrEqual({meaningful_count});"
        )
        .unwrap();
    } else if total_count > 0 {
        writeln!(
            out,
            "      expect(chunks.length, \"Expected at least one stream chunk\").toBeGreaterThan(0);"
        )
        .unwrap();
    }

    // Collect concatenated content.
    let expected_content: String = fixture
        .api
        .mock_response
        .stream_chunks
        .iter()
        .filter_map(|c| {
            c.get("choices")
                .and_then(|ch| ch.as_array())
                .and_then(|arr| arr.first())
                .and_then(|ch| ch.get("delta"))
                .and_then(|d| d.get("content"))
                .and_then(|v| v.as_str())
                .map(str::to_owned)
        })
        .collect();

    if !expected_content.is_empty() {
        writeln!(out, "      const content = chunks").unwrap();
        writeln!(out, "        .flatMap((c: unknown) => (c as {{ choices?: {{ delta?: {{ content?: string }} }}[] }}).choices ?? [])").unwrap();
        writeln!(
            out,
            "        .map((ch: {{ delta?: {{ content?: string }} }}) => ch.delta?.content ?? \"\")"
        )
        .unwrap();
        writeln!(out, "        .join(\"\");").unwrap();
        writeln!(
            out,
            "      expect(content, \"Stream content mismatch\").toBe({expected_content:?});"
        )
        .unwrap();
    }
}

