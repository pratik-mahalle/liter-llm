// ─── Assertion helpers ────────────────────────────────────────────────────────

fn emit_chat_assertions(out: &mut String, fixture: &Fixture) {
    let assertions = &fixture.assertions;

    if let Some(choices_arr) = fixture.api.mock_response.body.get("choices").and_then(|v| v.as_array()) {
        let count = choices_arr.len();
        writeln!(
            out,
            "      expect(response.choices.length, \"Choices count mismatch\").toBe({count});"
        )
        .unwrap();

        if let Some(first_choice) = choices_arr.first() {
            // finish_reason
            if let Some(fr) = first_choice
                .get("finish_reason")
                .and_then(|v| v.as_str())
                .filter(|s| !s.is_empty())
            {
                writeln!(
                    out,
                    "      expect(response.choices[0].finishReason, \"finish_reason mismatch\").toBe({fr:?});"
                )
                .unwrap();
            }

            // message content
            if let Some(content) = first_choice
                .get("message")
                .and_then(|m| m.get("content"))
                .and_then(|c| c.as_str())
            {
                writeln!(
                    out,
                    "      expect(response.choices[0].message.content, \"message content mismatch\").toBe({content:?});"
                )
                .unwrap();
            }

            // tool_calls presence
            if first_choice
                .get("message")
                .and_then(|m| m.get("tool_calls"))
                .and_then(|t| t.as_array())
                .is_some_and(|arr| !arr.is_empty())
            {
                writeln!(
                    out,
                    "      expect(Array.isArray(response.choices[0].message.toolCalls) && response.choices[0].message.toolCalls.length > 0, \"Expected tool_calls\").toBe(true);"
                )
                .unwrap();

                if let Some(fn_name) = first_choice
                    .get("message")
                    .and_then(|m| m.get("tool_calls"))
                    .and_then(|t| t.as_array())
                    .and_then(|arr| arr.first())
                    .and_then(|tc| tc.get("function"))
                    .and_then(|f| f.get("name"))
                    .and_then(|n| n.as_str())
                {
                    writeln!(
                        out,
                        "      expect(response.choices[0].message.toolCalls[0].function.name, \"tool call function name mismatch\").toBe({fn_name:?});"
                    )
                    .unwrap();
                }
            }
        }
    }

    // usage
    if let Some(usage) = fixture.api.mock_response.body.get("usage").filter(|v| !v.is_null()) {
        writeln!(
            out,
            "      expect(response.usage, \"Expected usage object\").toBeTruthy();"
        )
        .unwrap();
        if let Some(total) = usage.get("total_tokens").and_then(|v| v.as_u64()) {
            writeln!(
                out,
                "      expect(response.usage.totalTokens, \"total_tokens mismatch\").toBe({total});"
            )
            .unwrap();
        }
    }

    // model
    if let Some(model) = assertions
        .model
        .as_deref()
        .or_else(|| fixture.api.mock_response.body.get("model").and_then(|v| v.as_str()))
    {
        writeln!(out, "      expect(response.model, \"model mismatch\").toBe({model:?});").unwrap();
    }
}

fn emit_error_assertions(out: &mut String, fixture: &Fixture) {
    // If the fixture specifies an error_type, match on the bracket format from ManagedClient.
    if let Some(error_type) = &fixture.assertions.error_type {
        writeln!(
            out,
            "        expect((e as Error).message ?? \"\", \"Expected [{error_type}] error\").toMatch(/\\[{error_type}\\]|{error_type}/i);"
        )
        .unwrap();
        return;
    }

    // Fall back to generic status-based matching.
    let status = fixture.api.mock_response.status;
    match status {
        401 => writeln!(
            out,
            "        expect((e as Error).message ?? \"\", \"Expected auth error\").toMatch(/auth|unauthorized|401/i);"
        )
        .unwrap(),
        429 => writeln!(
            out,
            "        expect((e as Error).message ?? \"\", \"Expected rate limit error\").toMatch(/rate.?limit|429/i);"
        )
        .unwrap(),
        400 => writeln!(
            out,
            "        expect((e as Error).message ?? \"\", \"Expected bad request error\").toMatch(/bad.?request|400/i);"
        )
        .unwrap(),
        404 => writeln!(
            out,
            "        expect((e as Error).message ?? \"\", \"Expected not found error\").toMatch(/not.?found|404/i);"
        )
        .unwrap(),
        500 => writeln!(
            out,
            "        expect((e as Error).message ?? \"\", \"Expected server error\").toMatch(/server.?error|internal|500/i);"
        )
        .unwrap(),
        502 | 503 => writeln!(
            out,
            "        expect((e as Error).message ?? \"\", \"Expected service unavailable error\").toMatch(/unavailable|502|503/i);"
        )
        .unwrap(),
        _ => writeln!(out, "        // error thrown as expected").unwrap(),
    }
}

