/// New-category categories that get specialized test emitters instead of
/// standard per-fixture chat/embed tests.
const SPECIALIZED_CATEGORIES: &[&str] = &["cache", "budget", "hooks", "custom_provider"];

fn write_spec_file(dir: &Utf8Path, category: &str, fixtures: &[&Fixture]) -> Result<()> {
    // Parity fixtures are documentation-only; skip test generation entirely.
    if category == "parity" {
        return Ok(());
    }

    let mut out = String::new();

    writeln!(out, "// Code generated by liter-llm-e2e-generator. DO NOT EDIT.").unwrap();
    writeln!(
        out,
        r#"import {{ describe, it, expect, beforeAll, afterAll }} from "vitest";"#
    )
    .unwrap();
    writeln!(
        out,
        r#"import {{ startMockServer, type MockServer, type MockRoute }} from "./helpers";"#
    )
    .unwrap();
    writeln!(
        out,
        r#"// @ts-ignore – binding not yet implemented; tests compile and fail at runtime"#
    )
    .unwrap();
    writeln!(out, r#"import {{ LlmClient }} from "@kreuzberg/liter-llm";"#).unwrap();
    writeln!(out).unwrap();

    let describe_fn = "describe";
    writeln!(out, r#"{describe_fn}("{category}", () => {{"#).unwrap();

    for fixture in fixtures {
        // Skip fixtures targeting other languages.
        if fixture.skip.languages.iter().any(|l| l == "typescript") {
            continue;
        }

        if SPECIALIZED_CATEGORIES.contains(&category) {
            write_specialized_test_case(&mut out, fixture, category);
        } else {
            write_test_case(&mut out, fixture);
        }
    }

    writeln!(out, "}});").unwrap();

    let out = out.trim_end().to_string() + "\n";

    let safe_name = sanitize_name(category);
    let filename = format!("{safe_name}.spec.ts");
    let path = dir.join("tests").join(&filename);
    std::fs::write(&path, &out).map_err(|e| anyhow::anyhow!("Failed to write {filename}: {e}"))
}

/// Emit a single `it(...)` block for the fixture.
fn write_test_case(out: &mut String, fixture: &Fixture) {
    let method = fixture.api.method.as_str();
    let endpoint = endpoint_for_method(method);
    let http_method = match method {
        "list_models" | "retrieve_file" | "list_files" | "file_content" | "retrieve_batch" | "list_batches"
        | "retrieve_response" => "GET",
        "delete_file" | "cancel_batch" | "cancel_response" => "POST",
        _ => "POST",
    };
    let status = fixture.api.mock_response.status;

    writeln!(out, "  // {}", fixture.description).unwrap();
    writeln!(out, r#"  it("{}", async () => {{"#, fixture.id).unwrap();

    // Build the mock route.
    let body_json = serde_json::to_string(&fixture.api.mock_response.body).unwrap_or_default();
    // Escape backticks and backslashes for template literal safety.
    let body_escaped = body_json
        .replace('\\', "\\\\")
        .replace('`', "\\`")
        .replace("${", "\\${");

    writeln!(out, "    const routes: MockRoute[] = [").unwrap();
    writeln!(out, "      {{").unwrap();
    writeln!(out, "        path: {endpoint:?},").unwrap();
    writeln!(out, "        method: {http_method:?},").unwrap();
    writeln!(out, "        status: {status},").unwrap();
    writeln!(out, "        body: `{body_escaped}`,").unwrap();

    if fixture.api.mock_response.stream_chunks.is_empty() {
        writeln!(out, "        streamChunks: [],").unwrap();
    } else {
        writeln!(out, "        streamChunks: [").unwrap();
        for chunk in &fixture.api.mock_response.stream_chunks {
            let chunk_json = serde_json::to_string(chunk).unwrap_or_default();
            let chunk_escaped = chunk_json
                .replace('\\', "\\\\")
                .replace('`', "\\`")
                .replace("${", "\\${");
            writeln!(out, "          `{chunk_escaped}`,").unwrap();
        }
        writeln!(out, "        ],").unwrap();
    }

    writeln!(out, "      }},").unwrap();
    writeln!(out, "    ];").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "    const server = await startMockServer(routes);").unwrap();
    writeln!(out, "    try {{").unwrap();
    writeln!(
        out,
        r#"      const client = new LlmClient({{ apiKey: "test-key", baseUrl: server.url }});"#
    )
    .unwrap();
    writeln!(out).unwrap();

    // Emit API call and assertions.
    match method {
        "chat" => emit_chat_test(out, fixture),
        "chat_stream" => emit_stream_test(out, fixture),
        "embed" => emit_embed_test(out, fixture),
        "list_models" => emit_list_models_test(out, fixture),
        other => {
            writeln!(out, "      // TODO: unknown method {other:?}").unwrap();
        }
    }

    writeln!(out, "    }} finally {{").unwrap();
    writeln!(out, "      server.close();").unwrap();
    writeln!(out, "    }}").unwrap();
    writeln!(out, "  }});").unwrap();
    writeln!(out).unwrap();
}

