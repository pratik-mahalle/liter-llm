use anyhow::Result;
use camino::Utf8Path;

use crate::fixture::{Fixture, group_by_category};
use crate::generators::Generator;

pub struct TypeScriptGenerator;

impl Generator for TypeScriptGenerator {
    fn generate(&self, fixtures: &[Fixture], output_root: &Utf8Path) -> Result<()> {
        let ts_dir = output_root.join("typescript");

        // Clean only generated test files — preserve package.json and config managed by workspace.
        let tests_dir = ts_dir.join("tests");
        let _ = std::fs::remove_dir_all(&tests_dir);
        std::fs::create_dir_all(&tests_dir)
            .map_err(|e| anyhow::anyhow!("Failed to create typescript/tests dir: {e}"))?;

        write_vitest_config(&ts_dir)?;
        write_helpers(&ts_dir)?;

        let groups = group_by_category(fixtures);
        for (category, cat_fixtures) in &groups {
            write_spec_file(&ts_dir, category, cat_fixtures)?;
        }

        // Run biome formatter if available (best-effort; ignore errors).
        let _ = std::process::Command::new("biome")
            .args(["format", "--write"])
            .arg(ts_dir.as_str())
            .status();

        println!("Generated TypeScript e2e tests in {ts_dir}");
        Ok(())
    }
}

