fn write_mock_server(dir: &Utf8Path) -> Result<()> {
    let content = r#"# Code generated by liter-lm-e2e-generator. DO NOT EDIT.
defmodule LiterLmE2E.MockServer do
  @moduledoc """
  Plug-based mock HTTP server for liter-lm e2e tests.

  Routes are configured as a list of `%{path, method, status, body, stream_chunks}` maps.
  """

  use Plug.Builder

  plug(:dispatch)

  @doc """
  Start the mock server on a random port and return `{:ok, base_url}`.

  ## Examples

      routes = [%{path: "/chat/completions", method: "POST", status: 200, body: "{}", stream_chunks: []}]
      {:ok, url} = LiterLmE2E.MockServer.start(routes)
  """
  @spec start([map()]) :: {:ok, String.t()}
  def start(routes) do
    ref = make_ref() |> :erlang.ref_to_list() |> List.to_string()
    name = String.to_atom("mock_server_#{ref}")

    {:ok, _pid} =
      Plug.Cowboy.http(
        __MODULE__,
        [routes: routes],
        ref: name,
        port: 0
      )

    port = :ranch.get_port(name)
    url = "http://127.0.0.1:#{port}"
    {:ok, url}
  end

  @doc "Stop the mock server by its reference."
  @spec stop(reference()) :: :ok
  def stop(ref) do
    Plug.Cowboy.shutdown(ref)
    :ok
  end

  # Plug callback: match route and respond.
  def dispatch(conn, _opts) do
    routes = conn.private[:routes] || []
    path = conn.request_path
    method = conn.method |> String.upcase()

    case Enum.find(routes, fn r ->
           r.path == path and String.upcase(r.method) == method
         end) do
      nil ->
        conn
        |> Plug.Conn.send_resp(404, "No mock route for #{method} #{path}")

      route ->
        if route.stream_chunks != [] do
          conn =
            conn
            |> Plug.Conn.put_resp_header("content-type", "text/event-stream")
            |> Plug.Conn.put_resp_header("cache-control", "no-cache")
            |> Plug.Conn.send_chunked(route.status)

          Enum.each(route.stream_chunks, fn chunk ->
            Plug.Conn.chunk(conn, "data: #{chunk}\n\n")
          end)

          Plug.Conn.chunk(conn, "data: [DONE]\n\n")
          conn
        else
          conn
          |> Plug.Conn.put_resp_content_type("application/json")
          |> Plug.Conn.send_resp(route.status, route.body)
        end
    end
  end

  def init(opts), do: opts

  def call(conn, opts) do
    routes = Keyword.get(opts, :routes, [])

    conn
    |> Plug.Conn.put_private(:routes, routes)
    |> super(opts)
  end
end
"#;
    std::fs::write(dir.join("test").join("support").join("mock_server.ex"), content)
        .map_err(|e| anyhow::anyhow!("Failed to write test/support/mock_server.ex: {e}"))
}

fn write_helpers(dir: &Utf8Path) -> Result<()> {
    let content = r#"# Code generated by liter-lm-e2e-generator. DO NOT EDIT.
defmodule LiterLmE2E.Helpers do
  @moduledoc "Shared assertion helpers for liter-lm e2e tests."

  import ExUnit.Assertions

  @doc "Assert the number of choices in a chat response map."
  @spec assert_choices_count(map(), non_neg_integer()) :: :ok
  def assert_choices_count(response, expected) do
    actual = response |> Map.get("choices", []) |> length()
    assert actual == expected, "Expected #{expected} choice(s), got #{actual}"
    :ok
  end

  @doc "Assert the first choice message content."
  @spec assert_first_content(map(), String.t()) :: :ok
  def assert_first_content(response, expected) do
    actual =
      response
      |> Map.get("choices", [])
      |> List.first(%{})
      |> Map.get("message", %{})
      |> Map.get("content")

    assert actual == expected, "Expected content #{inspect(expected)}, got #{inspect(actual)}"
    :ok
  end

  @doc "Assert the model field."
  @spec assert_model(map(), String.t()) :: :ok
  def assert_model(response, expected) do
    actual = Map.get(response, "model")
    assert actual == expected, "Expected model #{inspect(expected)}, got #{inspect(actual)}"
    :ok
  end

  @doc "Assert stream chunks received."
  @spec assert_stream_chunks(list(), non_neg_integer()) :: :ok
  def assert_stream_chunks(chunks, min_count) do
    actual = length(chunks)
    assert actual >= min_count, "Expected at least #{min_count} chunk(s), got #{actual}"
    :ok
  end

  @doc "Parse SSE chunks from an HTTP response body."
  @spec collect_sse_chunks(binary()) :: list()
  def collect_sse_chunks(body) when is_binary(body) do
    body
    |> String.split("\n")
    |> Enum.filter(&String.starts_with?(&1, "data: "))
    |> Enum.map(&String.trim_leading(&1, "data: "))
    |> Enum.reject(&(&1 == "[DONE]"))
    |> Enum.map(&Jason.decode!/1)
  end
end
"#;
    std::fs::write(dir.join("test").join("support").join("helpers.ex"), content)
        .map_err(|e| anyhow::anyhow!("Failed to write test/support/helpers.ex: {e}"))
}

