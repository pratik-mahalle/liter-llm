fn write_test_file(dir: &Utf8Path, category: &str, fixtures: &[&Fixture]) -> Result<()> {
    let mut out = String::new();

    writeln!(out, "# Code generated by liter-lm-e2e-generator. DO NOT EDIT.").unwrap();
    writeln!(out, "defmodule LiterLmE2E.{}Test do", module_name(category)).unwrap();
    writeln!(out, "  use ExUnit.Case, async: true").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "  alias LiterLmE2E.MockServer").unwrap();
    writeln!(out, "  alias LiterLmE2E.Helpers").unwrap();
    writeln!(out).unwrap();

    for fixture in fixtures {
        if fixture.skip.languages.iter().any(|l| l == "elixir") {
            continue;
        }
        write_test_case(&mut out, fixture);
    }

    writeln!(out, "end").unwrap();

    let out = out.trim_end().to_string() + "\n";

    let filename = format!("{}_test.exs", sanitize_name(category));
    let path = dir.join("test").join(&filename);
    std::fs::write(&path, &out).map_err(|e| anyhow::anyhow!("Failed to write {filename}: {e}"))
}

fn write_test_case(out: &mut String, fixture: &Fixture) {
    let test_name = fixture.description.replace('"', "'");
    let method = fixture.api.method.as_str();
    let endpoint = endpoint_for_method(method);
    let http_method = match method {
        "list_models" | "retrieve_file" | "list_files" | "file_content" | "retrieve_batch" | "list_batches"
        | "retrieve_response" => "GET",
        "delete_file" | "cancel_batch" | "cancel_response" => "POST",
        _ => "POST",
    };
    let status = fixture.api.mock_response.status;
    let is_error = status >= 400 || !fixture.assertions.expect_success;

    writeln!(out).unwrap();
    writeln!(out, "  test {test_name:?} do").unwrap();

    // Build routes list.
    // Use {:?} (Rust debug format) directly on the JSON string — it wraps in double
    // quotes and escapes inner `"` as `\"`, which Elixir string literals decode
    // correctly to the original JSON.  Calling elixir_string_escape first would
    // produce double-escaped backslashes that Jason cannot parse.
    let body_json = serde_json::to_string(&fixture.api.mock_response.body).unwrap_or_default();

    writeln!(out, "    routes = [").unwrap();
    writeln!(out, "      %{{").unwrap();
    writeln!(out, "        path: {:?},", endpoint).unwrap();
    writeln!(out, "        method: {:?},", http_method).unwrap();
    writeln!(out, "        status: {status},").unwrap();
    writeln!(out, "        body: {:?},", body_json).unwrap();

    if fixture.api.mock_response.stream_chunks.is_empty() {
        writeln!(out, "        stream_chunks: []").unwrap();
    } else {
        writeln!(out, "        stream_chunks: [").unwrap();
        for chunk in &fixture.api.mock_response.stream_chunks {
            let chunk_json = serde_json::to_string(chunk).unwrap_or_default();
            writeln!(out, "          {:?},", chunk_json).unwrap();
        }
        writeln!(out, "        ]").unwrap();
    }

    writeln!(out, "      }}").unwrap();
    writeln!(out, "    ]").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "    {{:ok, base_url}} = MockServer.start(routes)").unwrap();
    writeln!(out).unwrap();

    match method {
        "chat_stream" => emit_stream_test(out, fixture, endpoint, is_error),
        _ => emit_http_test(out, fixture, endpoint, http_method, is_error),
    }

    writeln!(out, "  end").unwrap();
}

fn emit_http_test(out: &mut String, fixture: &Fixture, endpoint: &str, http_method: &str, is_error: bool) {
    let req_json = serde_json::to_string(&fixture.api.request).unwrap_or_default();

    let method_lower = http_method.to_lowercase();

    if http_method == "GET" {
        writeln!(
            out,
            "    {{:ok, resp}} = Req.{method_lower}(base_url <> {:?}, decode_body: false)",
            endpoint
        )
        .unwrap();
    } else {
        writeln!(
            out,
            "    {{:ok, resp}} = Req.{method_lower}(base_url <> {:?}, body: {:?}, headers: [{{\"content-type\", \"application/json\"}}], decode_body: false)",
            endpoint, req_json
        ).unwrap();
    }

    if is_error {
        writeln!(
            out,
            "    assert resp.status == {status}",
            status = fixture.api.mock_response.status
        )
        .unwrap();
    } else {
        writeln!(out, "    assert resp.status == 200").unwrap();
        writeln!(out, "    doc = Jason.decode!(resp.body)").unwrap();
        writeln!(out).unwrap();
        emit_elixir_assertions(out, fixture, fixture.api.method.as_str());
    }
}

fn emit_stream_test(out: &mut String, fixture: &Fixture, endpoint: &str, is_error: bool) {
    let req_json = serde_json::to_string(&fixture.api.request).unwrap_or_default();

    // Use decode_body: false so the SSE body is received as a raw binary
    // (Plug.Cowboy buffers chunked responses in tests; into: :self returns
    // Req.Response.Async which is not a binary).
    writeln!(
        out,
        "    {{:ok, resp}} = Req.post(base_url <> {:?}, body: {:?}, headers: [{{\"content-type\", \"application/json\"}}], decode_body: false)",
        endpoint, req_json
    ).unwrap();

    if is_error {
        let status = fixture.api.mock_response.status;
        writeln!(out, "    assert resp.status == {status}").unwrap();
        return;
    }

    writeln!(out, "    assert resp.status == 200").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "    chunks = LiterLmE2E.Helpers.collect_sse_chunks(resp.body)").unwrap();

    if fixture.api.mock_response.stream_chunks.is_empty() {
        writeln!(out, "    assert length(chunks) == 0").unwrap();
    } else {
        let meaningful: usize = fixture
            .api
            .mock_response
            .stream_chunks
            .iter()
            .filter(|c| {
                c.get("choices")
                    .and_then(|ch| ch.as_array())
                    .and_then(|arr| arr.first())
                    .and_then(|ch| ch.get("delta"))
                    .and_then(|d| d.get("content"))
                    .and_then(|v| v.as_str())
                    .is_some_and(|s| !s.is_empty())
            })
            .count();
        let min_chunks = meaningful.max(1);

        writeln!(
            out,
            "    assert length(chunks) >= {min_chunks}, \"Expected at least {min_chunks} chunk(s), got #{{length(chunks)}}\""
        ).unwrap();
    }
}

fn emit_elixir_assertions(out: &mut String, fixture: &Fixture, method: &str) {
    match method {
        "chat" => {
            if let Some(choices_arr) = fixture.api.mock_response.body.get("choices").and_then(|v| v.as_array()) {
                let count = choices_arr.len();
                writeln!(
                    out,
                    "    assert length(doc[\"choices\"]) == {count}, \"Expected {count} choice(s)\""
                )
                .unwrap();

                if let Some(first_choice) = choices_arr.first() {
                    if let Some(content) = first_choice
                        .get("message")
                        .and_then(|m| m.get("content"))
                        .and_then(|c| c.as_str())
                    {
                        writeln!(
                            out,
                            "    assert get_in(doc, [\"choices\", Access.at(0), \"message\", \"content\"]) == {:?}",
                            content
                        )
                        .unwrap();
                    }

                    if let Some(fr) = first_choice
                        .get("finish_reason")
                        .and_then(|v| v.as_str())
                        .filter(|s| !s.is_empty())
                    {
                        writeln!(
                            out,
                            "    assert get_in(doc, [\"choices\", Access.at(0), \"finish_reason\"]) == {:?}",
                            fr
                        )
                        .unwrap();
                    }
                }
            }

            if let Some(usage) = fixture.api.mock_response.body.get("usage").filter(|v| !v.is_null())
                && let Some(total) = usage.get("total_tokens").and_then(|v| v.as_u64())
            {
                writeln!(out, "    assert get_in(doc, [\"usage\", \"total_tokens\"]) == {total}").unwrap();
            }

            let model = fixture
                .assertions
                .model
                .as_deref()
                .or_else(|| fixture.api.mock_response.body.get("model").and_then(|v| v.as_str()));
            if let Some(model) = model {
                writeln!(out, "    assert doc[\"model\"] == {:?}", model).unwrap();
            }
        }
        "embed" => {
            if let Some(data_arr) = fixture.api.mock_response.body.get("data").and_then(|v| v.as_array()) {
                let count = data_arr.len();
                writeln!(out, "    assert length(doc[\"data\"]) == {count}").unwrap();
            }
        }
        "list_models" => {
            if let Some(data_arr) = fixture.api.mock_response.body.get("data").and_then(|v| v.as_array()) {
                let count = data_arr.len();
                writeln!(out, "    assert length(doc[\"data\"]) == {count}").unwrap();
            }
        }
        _ => {}
    }
}

