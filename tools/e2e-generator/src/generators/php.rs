use std::fmt::Write as FmtWrite;

use anyhow::Result;
use camino::Utf8Path;

use crate::fixture::{Fixture, group_by_category};
use crate::generators::Generator;

pub struct PhpGenerator;

impl Generator for PhpGenerator {
    fn name(&self) -> &'static str {
        "php"
    }

    fn generate(&self, fixtures: &[Fixture], output_root: &Utf8Path) -> Result<()> {
        let php_dir = output_root.join("php");

        // Clean stale output before regenerating.
        let _ = std::fs::remove_dir_all(&php_dir);

        std::fs::create_dir_all(php_dir.join("tests"))
            .map_err(|e| anyhow::anyhow!("Failed to create php/tests dir: {e}"))?;

        write_composer_json(&php_dir)?;
        write_phpunit_xml(&php_dir)?;
        write_helpers(&php_dir)?;

        let groups = group_by_category(fixtures);
        for (category, cat_fixtures) in &groups {
            write_test_file(&php_dir, category, cat_fixtures)?;
        }

        // Run php-cs-fixer if available (best-effort; ignore errors).
        let _ = std::process::Command::new("php-cs-fixer")
            .args(["fix", "--rules=@PSR12", "--quiet", "tests/"])
            .current_dir(&php_dir)
            .status();

        println!("Generated PHP e2e tests in {php_dir}");
        Ok(())
    }
}

// ─── File writers ─────────────────────────────────────────────────────────────

fn write_composer_json(dir: &Utf8Path) -> Result<()> {
    // JSON does not support comments; the generation notice is placed in the
    // "description" field and in phpunit.xml instead.
    let content = r#"{
    "name": "liter-lm/e2e-tests",
    "description": "Generated e2e tests for liter-lm PHP binding. Code generated by liter-lm-e2e-generator. DO NOT EDIT.",
    "type": "project",
    "require": {
        "php": ">=8.2"
    },
    "require-dev": {
        "phpunit/phpunit": "^11.0"
    },
    "autoload-dev": {
        "psr-4": {
            "LiterLmE2E\\": "tests/"
        }
    }
}
"#;
    std::fs::write(dir.join("composer.json"), content)
        .map_err(|e| anyhow::anyhow!("Failed to write composer.json: {e}"))
}

fn write_phpunit_xml(dir: &Utf8Path) -> Result<()> {
    let content = r#"<?xml version="1.0" encoding="UTF-8"?>
<!-- Code generated by liter-lm-e2e-generator. DO NOT EDIT. -->
<phpunit xmlns:xsi="http://www.w3.org/2001/XMLSchema-instance"
         xsi:noNamespaceSchemaLocation="https://schema.phpunit.de/11.0/phpunit.xsd"
         bootstrap="vendor/autoload.php"
         colors="true">
    <testsuites>
        <testsuite name="e2e">
            <directory>tests</directory>
        </testsuite>
    </testsuites>
    <source>
        <include>
            <directory>tests</directory>
        </include>
    </source>
</phpunit>
"#;
    std::fs::write(dir.join("phpunit.xml"), content).map_err(|e| anyhow::anyhow!("Failed to write phpunit.xml: {e}"))
}

fn write_helpers(dir: &Utf8Path) -> Result<()> {
    let content = r#"<?php

declare(strict_types=1);

// Code generated by liter-lm-e2e-generator. DO NOT EDIT.

namespace LiterLmE2E;

/**
 * Configuration for a single mock HTTP route.
 */
final class MockRoute
{
    public function __construct(
        public readonly string $path,
        public readonly string $method,
        public readonly int $status,
        public readonly string $body,
        /** @var list<string> */
        public readonly array $streamChunks = [],
    ) {}
}

/**
 * Minimal PHP built-in server-based mock HTTP server for liter-lm e2e tests.
 *
 * Writes a router script to a temp file, launches `php -S` in the background,
 * waits for it to be ready, then serves mock routes.
 */
final class MockServer
{
    public readonly string $url;
    private readonly string $routerFile;
    /** @var resource */
    private $process;

    /**
     * @param list<MockRoute> $routes
     */
    public function __construct(array $routes)
    {
        $port = $this->findFreePort();
        $this->url = "http://127.0.0.1:{$port}";

        // Encode routes as PHP data so the router script can use them.
        $routeData = json_encode(
            array_map(static fn (MockRoute $r) => [
                'path'         => $r->path,
                'method'       => strtoupper($r->method),
                'status'       => $r->status,
                'body'         => $r->body,
                'streamChunks' => $r->streamChunks,
            ], $routes),
            JSON_THROW_ON_ERROR
        );

        $this->routerFile = tempnam(sys_get_temp_dir(), 'liter_lm_mock_') . '.php';
        file_put_contents($this->routerFile, $this->buildRouterScript($routeData));

        $descriptorSpec = [
            0 => ['pipe', 'r'],
            1 => ['pipe', 'w'],
            2 => ['pipe', 'w'],
        ];

        $pipes = [];
        $this->process = proc_open(
            [PHP_BINARY, '-S', "127.0.0.1:{$port}", $this->routerFile],
            $descriptorSpec,
            $pipes,
            sys_get_temp_dir()
        );

        // Wait until the server is listening (up to 3 seconds).
        $deadline = microtime(true) + 3.0;
        while (microtime(true) < $deadline) {
            $sock = @fsockopen('127.0.0.1', $port, $errno, $errstr, 0.1);
            if ($sock !== false) {
                fclose($sock);
                break;
            }
            usleep(50_000);
        }
    }

    public function stop(): void
    {
        if (is_resource($this->process)) {
            proc_terminate($this->process);
            proc_close($this->process);
        }
        if (file_exists($this->routerFile)) {
            unlink($this->routerFile);
        }
    }

    private function findFreePort(): int
    {
        $sock = socket_create(AF_INET, SOCK_STREAM, SOL_TCP);
        socket_bind($sock, '127.0.0.1', 0);
        socket_getsockname($sock, $addr, $port);
        socket_close($sock);
        return (int) $port;
    }

    private function buildRouterScript(string $routeDataJson): string
    {
        // phpcs:disable
        return <<<PHP
        <?php
        \$routes = json_decode('{$routeDataJson}', true);
        \$path   = parse_url(\$_SERVER['REQUEST_URI'], PHP_URL_PATH);
        \$method = strtoupper(\$_SERVER['REQUEST_METHOD']);

        foreach (\$routes as \$route) {
            if (\$route['path'] === \$path && \$route['method'] === \$method) {
                http_response_code(\$route['status']);
                if (!empty(\$route['streamChunks'])) {
                    header('Content-Type: text/event-stream');
                    header('Cache-Control: no-cache');
                    foreach (\$route['streamChunks'] as \$chunk) {
                        echo "data: " . \$chunk . "\n\n";
                        ob_flush();
                        flush();
                    }
                    echo "data: [DONE]\n\n";
                } else {
                    header('Content-Type: application/json');
                    echo \$route['body'];
                }
                exit;
            }
        }
        http_response_code(404);
        echo "No mock route for \$method \$path";
        PHP;
        // phpcs:enable
    }
}

/**
 * Shared HTTP helper: send a request to the mock server and return body + status.
 *
 * @return array{status: int, body: string}
 */
function httpRequest(string $url, string $method, string $body = ''): array
{
    $ctx = stream_context_create([
        'http' => [
            'method'  => strtoupper($method),
            'header'  => "Content-Type: application/json\r\n",
            'content' => $body,
            'ignore_errors' => true,
        ],
    ]);

    $responseBody = file_get_contents($url, false, $ctx);
    $meta         = stream_get_meta_data($ctx);

    // Parse status from response headers.
    $status = 200;
    if (!empty($http_response_header)) {
        preg_match('/HTTP\/\S+\s+(\d+)/', $http_response_header[0], $m);
        $status = (int) ($m[1] ?? 200);
    }

    return [
        'status' => $status,
        'body'   => $responseBody !== false ? $responseBody : '',
    ];
}

/**
 * Read SSE stream from URL and return data payloads (excluding [DONE]).
 *
 * @return list<string>
 */
function readSseChunks(string $url, string $method, string $body = ''): array
{
    $ctx = stream_context_create([
        'http' => [
            'method'         => strtoupper($method),
            'header'         => "Content-Type: application/json\r\n",
            'content'        => $body,
            'ignore_errors'  => true,
        ],
    ]);

    $stream = fopen($url, 'r', false, $ctx);
    if ($stream === false) {
        return [];
    }

    $chunks = [];
    while (!feof($stream)) {
        $line = fgets($stream);
        if ($line === false) {
            break;
        }
        $line = rtrim($line);
        if (str_starts_with($line, 'data: ') && !str_contains($line, '[DONE]')) {
            $chunks[] = substr($line, 6);
        }
    }
    fclose($stream);

    return $chunks;
}
"#;
    std::fs::write(dir.join("tests").join("Helpers.php"), content)
        .map_err(|e| anyhow::anyhow!("Failed to write tests/Helpers.php: {e}"))
}

