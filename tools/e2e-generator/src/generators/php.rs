fn write_test_file(dir: &Utf8Path, category: &str, fixtures: &[&Fixture]) -> Result<()> {
    let mut out = String::new();

    writeln!(out, "<?php").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "declare(strict_types=1);").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "// Code generated by liter-lm-e2e-generator. DO NOT EDIT.").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "namespace LiterLmE2E;").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "use PHPUnit\\Framework\\TestCase;").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "require_once __DIR__ . '/Helpers.php';").unwrap();
    writeln!(out).unwrap();

    let class_name = pascal_case(category);
    writeln!(out, "final class {class_name}Test extends TestCase").unwrap();
    writeln!(out, "{{").unwrap();

    let mut wrote_any = false;
    for fixture in fixtures {
        if fixture.skip.languages.iter().any(|l| l == "php") {
            continue;
        }
        write_test_method(&mut out, fixture);
        wrote_any = true;
    }

    if !wrote_any {
        writeln!(out, "    public function testPlaceholder(): void").unwrap();
        writeln!(out, "    {{").unwrap();
        writeln!(out, "        $this->assertTrue(true);").unwrap();
        writeln!(out, "    }}").unwrap();
    }

    writeln!(out, "}}").unwrap();

    let out = out.trim_end().to_string() + "\n";

    let filename = format!("{}Test.php", pascal_case(category));
    let path = dir.join("tests").join(&filename);
    std::fs::write(&path, &out).map_err(|e| anyhow::anyhow!("Failed to write {filename}: {e}"))
}

fn write_test_method(out: &mut String, fixture: &Fixture) {
    let method_name = camel_case(&fixture.id);
    let method = fixture.api.method.as_str();
    let endpoint = endpoint_for_method(method);
    let http_method = if method == "list_models" { "GET" } else { "POST" };
    let status = fixture.api.mock_response.status;
    let is_error = status >= 400 || !fixture.assertions.expect_success;

    writeln!(out).unwrap();
    writeln!(out, "    /** {} */", fixture.description).unwrap();
    writeln!(out, "    public function test{method_name}(): void").unwrap();
    writeln!(out, "    {{").unwrap();

    // Build mock route.
    let body_json = serde_json::to_string(&fixture.api.mock_response.body).unwrap_or_default();
    let body_php = php_string_escape(&body_json);

    writeln!(out, "        $routes = [").unwrap();
    writeln!(out, "            new MockRoute(").unwrap();
    writeln!(out, "                path: '{endpoint}',").unwrap();
    writeln!(out, "                method: '{http_method}',").unwrap();
    writeln!(out, "                status: {status},").unwrap();
    writeln!(out, "                body: '{body_php}',").unwrap();

    if fixture.api.mock_response.stream_chunks.is_empty() {
        writeln!(out, "                streamChunks: [],").unwrap();
    } else {
        writeln!(out, "                streamChunks: [").unwrap();
        for chunk in &fixture.api.mock_response.stream_chunks {
            let chunk_json = serde_json::to_string(chunk).unwrap_or_default();
            let chunk_php = php_string_escape(&chunk_json);
            writeln!(out, "                    '{chunk_php}',").unwrap();
        }
        writeln!(out, "                ],").unwrap();
    }

    writeln!(out, "            ),").unwrap();
    writeln!(out, "        ];").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "        $server = new MockServer($routes);").unwrap();

    match method {
        "chat_stream" => {
            let req_json = serde_json::to_string(&fixture.api.request).unwrap_or_default();
            let req_php = php_string_escape(&req_json);
            writeln!(
                out,
                "        $chunks = readSseChunks($server->url . '{endpoint}', 'POST', '{req_php}');"
            )
            .unwrap();
            writeln!(out, "        $server->stop();").unwrap();
            writeln!(out).unwrap();

            let meaningful: usize = fixture
                .api
                .mock_response
                .stream_chunks
                .iter()
                .filter(|c| {
                    c.get("choices")
                        .and_then(|ch| ch.as_array())
                        .and_then(|arr| arr.first())
                        .and_then(|ch| ch.get("delta"))
                        .and_then(|d| d.get("content"))
                        .and_then(|v| v.as_str())
                        .is_some_and(|s| !s.is_empty())
                })
                .count();
            let min_chunks = meaningful.max(1);

            writeln!(
                out,
                "        $this->assertGreaterThanOrEqual({min_chunks}, count($chunks), 'Expected at least {min_chunks} chunk(s)');"
            ).unwrap();
        }
        _ => {
            let req_json = serde_json::to_string(&fixture.api.request).unwrap_or_default();
            let req_php = php_string_escape(&req_json);

            if http_method == "GET" {
                writeln!(
                    out,
                    "        $result = httpRequest($server->url . '{endpoint}', 'GET');"
                )
                .unwrap();
            } else {
                writeln!(
                    out,
                    "        $result = httpRequest($server->url . '{endpoint}', 'POST', '{req_php}');"
                )
                .unwrap();
            }

            writeln!(out, "        $server->stop();").unwrap();
            writeln!(out).unwrap();

            if is_error {
                writeln!(out, "        $this->assertEquals({status}, $result['status']);").unwrap();
            } else {
                writeln!(out, "        $this->assertEquals(200, $result['status']);").unwrap();
                writeln!(
                    out,
                    "        $doc = json_decode($result['body'], true, 512, JSON_THROW_ON_ERROR);"
                )
                .unwrap();
                writeln!(out).unwrap();
                emit_php_assertions(out, fixture, method);
            }
        }
    }

    writeln!(out, "    }}").unwrap();
}

fn emit_php_assertions(out: &mut String, fixture: &Fixture, method: &str) {
    match method {
        "chat" => {
            if let Some(choices_arr) = fixture.api.mock_response.body.get("choices").and_then(|v| v.as_array()) {
                let count = choices_arr.len();
                writeln!(out, "        $this->assertCount({count}, $doc['choices']);").unwrap();

                if let Some(first_choice) = choices_arr.first() {
                    if let Some(content) = first_choice
                        .get("message")
                        .and_then(|m| m.get("content"))
                        .and_then(|c| c.as_str())
                    {
                        let c_php = php_string_escape(content);
                        writeln!(
                            out,
                            "        $this->assertEquals('{c_php}', $doc['choices'][0]['message']['content']);"
                        )
                        .unwrap();
                    }

                    if let Some(fr) = first_choice
                        .get("finish_reason")
                        .and_then(|v| v.as_str())
                        .filter(|s| !s.is_empty())
                    {
                        let fr_php = php_string_escape(fr);
                        writeln!(
                            out,
                            "        $this->assertEquals('{fr_php}', $doc['choices'][0]['finish_reason']);"
                        )
                        .unwrap();
                    }
                }
            }

            if let Some(usage) = fixture.api.mock_response.body.get("usage").filter(|v| !v.is_null())
                && let Some(total) = usage.get("total_tokens").and_then(|v| v.as_u64())
            {
                writeln!(
                    out,
                    "        $this->assertEquals({total}, $doc['usage']['total_tokens']);"
                )
                .unwrap();
            }

            let model = fixture
                .assertions
                .model
                .as_deref()
                .or_else(|| fixture.api.mock_response.body.get("model").and_then(|v| v.as_str()));
            if let Some(model) = model {
                let m_php = php_string_escape(model);
                writeln!(out, "        $this->assertEquals('{m_php}', $doc['model']);").unwrap();
            }
        }
        "embed" => {
            if let Some(data_arr) = fixture.api.mock_response.body.get("data").and_then(|v| v.as_array()) {
                let count = data_arr.len();
                writeln!(out, "        $this->assertCount({count}, $doc['data']);").unwrap();
            }
        }
        "list_models" => {
            if let Some(data_arr) = fixture.api.mock_response.body.get("data").and_then(|v| v.as_array()) {
                let count = data_arr.len();
                writeln!(out, "        $this->assertCount({count}, $doc['data']);").unwrap();
            }
        }
        _ => {}
    }
}

// ─── Helpers ──────────────────────────────────────────────────────────────────

fn endpoint_for_method(method: &str) -> &'static str {
    match method {
        "chat" | "chat_stream" => "/chat/completions",
        "embed" => "/embeddings",
        "list_models" => "/models",
        _ => "/chat/completions",
    }
}

/// Escape a string for use in a PHP single-quoted string literal.
fn php_string_escape(s: &str) -> String {
    s.replace('\\', "\\\\").replace('\'', "\\'")
}

fn pascal_case(name: &str) -> String {
    name.split(['_', '-', ' '])
        .filter(|s| !s.is_empty())
        .map(|word| {
            let mut chars = word.chars();
            match chars.next() {
                None => String::new(),
                Some(first) => {
                    let upper: String = first.to_uppercase().collect();
                    upper + chars.as_str()
                }
            }
        })
        .collect()
}

fn camel_case(name: &str) -> String {
    pascal_case(name)
}
