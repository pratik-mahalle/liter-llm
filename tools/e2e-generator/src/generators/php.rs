pub struct PhpGenerator;

impl Generator for PhpGenerator {
    fn generate(&self, fixtures: &[Fixture], output_root: &Utf8Path) -> Result<()> {
        let php_dir = output_root.join("php");

        // Clean stale output before regenerating.
        let _ = std::fs::remove_dir_all(&php_dir);

        std::fs::create_dir_all(php_dir.join("tests"))
            .map_err(|e| anyhow::anyhow!("Failed to create php/tests dir: {e}"))?;

        write_composer_json(&php_dir)?;
        write_phpunit_xml(&php_dir)?;
        write_helpers(&php_dir)?;

        let groups = group_by_category(fixtures);
        for (category, cat_fixtures) in &groups {
            write_test_file(&php_dir, category, cat_fixtures)?;
        }

        // Run php-cs-fixer if available (best-effort; ignore errors).
        let _ = std::process::Command::new("php-cs-fixer")
            .args(["fix", "--rules=@PSR12", "--quiet", "tests/"])
            .current_dir(&php_dir)
            .status();

        println!("Generated PHP e2e tests in {php_dir}");
        Ok(())
    }
}

