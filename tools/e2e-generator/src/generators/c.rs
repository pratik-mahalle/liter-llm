// ─── Test file generation ─────────────────────────────────────────────────────

fn write_test_file(dir: &Utf8Path, category: &str, fixtures: &[&Fixture]) -> Result<()> {
    let mut out = String::new();

    writeln!(out, "/* Code generated by liter-lm-e2e-generator. DO NOT EDIT. */").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "#include \"helpers.h\"").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "#include <assert.h>").unwrap();
    writeln!(out, "#include <stdio.h>").unwrap();
    writeln!(out, "#include <stdlib.h>").unwrap();
    writeln!(out, "#include <string.h>").unwrap();
    writeln!(out).unwrap();

    let mut wrote_any = false;
    for fixture in fixtures {
        if fixture.skip.languages.iter().any(|l| l == "c") {
            continue;
        }
        write_test_fn(&mut out, fixture);
        wrote_any = true;
    }

    // main() runs all tests.
    writeln!(out, "int main(void)").unwrap();
    writeln!(out, "{{").unwrap();

    if wrote_any {
        for fixture in fixtures {
            if fixture.skip.languages.iter().any(|l| l == "c") {
                continue;
            }
            let fn_name = sanitize_name(&fixture.id);
            writeln!(out, "    test_{fn_name}();").unwrap();
            writeln!(
                out,
                "    printf(\"PASS: {}\\n\");",
                c_string_escape(&fixture.description)
            )
            .unwrap();
        }
    }

    writeln!(out, "    printf(\"All {} tests passed.\\n\");", sanitize_name(category)).unwrap();
    writeln!(out, "    return 0;").unwrap();
    writeln!(out, "}}").unwrap();

    let out = out.trim_end().to_string() + "\n";

    let filename = format!("test_{}.c", sanitize_name(category));
    let path = dir.join(&filename);
    std::fs::write(&path, &out).map_err(|e| anyhow::anyhow!("Failed to write {filename}: {e}"))
}

fn write_test_fn(out: &mut String, fixture: &Fixture) {
    let fn_name = sanitize_name(&fixture.id);
    let method = fixture.api.method.as_str();
    let endpoint = endpoint_for_method(method);
    let http_method = match method {
        "list_models" | "retrieve_file" | "list_files" | "file_content" | "retrieve_batch" | "list_batches"
        | "retrieve_response" => "GET",
        "delete_file" | "cancel_batch" | "cancel_response" => "POST",
        _ => "POST",
    };
    let status = fixture.api.mock_response.status;
    let is_error = status >= 400 || !fixture.assertions.expect_success;

    writeln!(out, "/* {} */", fixture.description).unwrap();
    writeln!(out, "static void test_{fn_name}(void)").unwrap();
    writeln!(out, "{{").unwrap();

    // For C, we use pre-recorded JSON (the mock response body) instead of a live mock server.
    // The test makes a real HTTP call only if a LITER_LM_TEST_BASE_URL env var is set;
    // otherwise it performs a unit-style assertion against the pre-recorded body.
    let body_json = serde_json::to_string(&fixture.api.mock_response.body).unwrap_or_default();
    let body_c = c_string_escape(&body_json);

    writeln!(out, "    /* Pre-recorded mock response body. */").unwrap();
    writeln!(out, "    const char *mock_body = \"{body_c}\";").unwrap();
    writeln!(out).unwrap();

    writeln!(out, "    const char *base_url = getenv(\"LITER_LM_TEST_BASE_URL\");").unwrap();
    writeln!(out, "    if (base_url != NULL) {{").unwrap();
    writeln!(out, "        /* Live HTTP test against a real server. */").unwrap();

    let req_json = serde_json::to_string(&fixture.api.request).unwrap_or_default();
    let req_c = c_string_escape(&req_json);

    // Build full URL strings.
    writeln!(out, "        char url[1024];").unwrap();
    writeln!(out, "        snprintf(url, sizeof(url), \"%s{endpoint}\", base_url);").unwrap();
    writeln!(out).unwrap();

    if method == "chat_stream" {
        if is_error {
            // For error stream fixtures, assert the HTTP response status code instead.
            writeln!(
                out,
                "        LiterLmResponse *resp = liter_lm_http_post(url, \"{req_c}\");"
            )
            .unwrap();
            writeln!(out, "        assert(resp != NULL);").unwrap();
            writeln!(
                out,
                "        liter_lm_assert_status(resp, {status}L, \"test_{fn_name}\");"
            )
            .unwrap();
            writeln!(out, "        liter_lm_response_free(resp);").unwrap();
        } else {
            writeln!(out, "        char *chunks[256];").unwrap();
            writeln!(out, "        int n = liter_lm_read_sse(url, \"{req_c}\", chunks, 256);").unwrap();

            if fixture.api.mock_response.stream_chunks.is_empty() {
                writeln!(out, "        assert(n == 0);").unwrap();
            } else {
                let meaningful: usize = fixture
                    .api
                    .mock_response
                    .stream_chunks
                    .iter()
                    .filter(|c| {
                        c.get("choices")
                            .and_then(|ch| ch.as_array())
                            .and_then(|arr| arr.first())
                            .and_then(|ch| ch.get("delta"))
                            .and_then(|d| d.get("content"))
                            .and_then(|v| v.as_str())
                            .is_some_and(|s| !s.is_empty())
                    })
                    .count();
                let min_chunks = meaningful.max(1);

                writeln!(out, "        if (n < {min_chunks}) {{").unwrap();
                writeln!(
                    out,
                    "            fprintf(stderr, \"FAIL [test_{fn_name}]: expected >= {min_chunks} chunks, got %d\\n\", n);"
                )
                .unwrap();
                writeln!(out, "            abort();").unwrap();
                writeln!(out, "        }}").unwrap();
            }
            writeln!(out, "        for (int i = 0; i < n; i++) free(chunks[i]);").unwrap();
        }
    } else if http_method == "GET" {
        writeln!(out, "        LiterLmResponse *resp = liter_lm_http_get(url);").unwrap();
        writeln!(out, "        assert(resp != NULL);").unwrap();

        if is_error {
            writeln!(
                out,
                "        liter_lm_assert_status(resp, {status}L, \"test_{fn_name}\");"
            )
            .unwrap();
        } else {
            writeln!(out, "        liter_lm_assert_status(resp, 200L, \"test_{fn_name}\");").unwrap();
            emit_c_json_assertions(out, fixture, "resp->body", &format!("test_{fn_name}"));
        }

        writeln!(out, "        liter_lm_response_free(resp);").unwrap();
    } else {
        writeln!(
            out,
            "        LiterLmResponse *resp = liter_lm_http_post(url, \"{req_c}\");"
        )
        .unwrap();
        writeln!(out, "        assert(resp != NULL);").unwrap();

        if is_error {
            writeln!(
                out,
                "        liter_lm_assert_status(resp, {status}L, \"test_{fn_name}\");"
            )
            .unwrap();
        } else {
            writeln!(out, "        liter_lm_assert_status(resp, 200L, \"test_{fn_name}\");").unwrap();
            emit_c_json_assertions(out, fixture, "resp->body", &format!("test_{fn_name}"));
        }

        writeln!(out, "        liter_lm_response_free(resp);").unwrap();
    }

    writeln!(out, "    }} else {{").unwrap();
    writeln!(out, "        /* Offline: assert against pre-recorded mock body. */").unwrap();

    if !is_error && method != "chat_stream" {
        emit_c_json_assertions(out, fixture, "mock_body", &format!("test_{fn_name}"));
    } else {
        writeln!(
            out,
            "        (void)mock_body; /* error or stream test — skip offline assertions */"
        )
        .unwrap();
    }

    writeln!(out, "    }}").unwrap();
    writeln!(out, "}}").unwrap();
    writeln!(out).unwrap();
}

fn emit_c_json_assertions(out: &mut String, fixture: &Fixture, body_var: &str, test_name: &str) {
    let method = fixture.api.method.as_str();

    match method {
        "chat" => {
            if let Some(choices_arr) = fixture.api.mock_response.body.get("choices").and_then(|v| v.as_array()) {
                let count = choices_arr.len() as i64;
                writeln!(
                    out,
                    "        liter_lm_assert_json_array_len({body_var}, \"choices\", {count}, \"{test_name}\");"
                )
                .unwrap();

                if let Some(first_choice) = choices_arr.first()
                    && let Some(content) = first_choice
                        .get("message")
                        .and_then(|m| m.get("content"))
                        .and_then(|c| c.as_str())
                {
                    let c_escaped = c_string_escape(content);
                    writeln!(
                        out,
                        "        liter_lm_assert_json_field({body_var}, \"content\", \"{c_escaped}\", \"{test_name}\");"
                    )
                    .unwrap();
                }
            }

            let model = fixture
                .assertions
                .model
                .as_deref()
                .or_else(|| fixture.api.mock_response.body.get("model").and_then(|v| v.as_str()));
            if let Some(model) = model {
                let m_escaped = c_string_escape(model);
                writeln!(
                    out,
                    "        liter_lm_assert_json_field({body_var}, \"model\", \"{m_escaped}\", \"{test_name}\");"
                )
                .unwrap();
            }
        }
        "embed" => {
            if let Some(data_arr) = fixture.api.mock_response.body.get("data").and_then(|v| v.as_array()) {
                let count = data_arr.len() as i64;
                writeln!(
                    out,
                    "        liter_lm_assert_json_array_len({body_var}, \"data\", {count}, \"{test_name}\");"
                )
                .unwrap();
            }
        }
        "list_models" => {
            if let Some(data_arr) = fixture.api.mock_response.body.get("data").and_then(|v| v.as_array()) {
                let count = data_arr.len() as i64;
                writeln!(
                    out,
                    "        liter_lm_assert_json_array_len({body_var}, \"data\", {count}, \"{test_name}\");"
                )
                .unwrap();
            }
        }
        _ => {}
    }
}

