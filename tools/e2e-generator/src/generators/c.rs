use std::fmt::Write as FmtWrite;

use anyhow::Result;
use camino::Utf8Path;

use crate::fixture::{Fixture, group_by_category};
use crate::generators::Generator;

pub struct CGenerator;

impl Generator for CGenerator {
    fn generate(&self, fixtures: &[Fixture], output_root: &Utf8Path) -> Result<()> {
        let c_dir = output_root.join("c");

        // Clean stale output before regenerating.
        let _ = std::fs::remove_dir_all(&c_dir);

        std::fs::create_dir_all(&c_dir).map_err(|e| anyhow::anyhow!("Failed to create c dir: {e}"))?;

        write_makefile(&c_dir)?;
        write_helpers_h(&c_dir)?;
        write_helpers_c(&c_dir)?;
        write_run_tests_sh(&c_dir)?;

        let groups = group_by_category(fixtures);
        for (category, cat_fixtures) in &groups {
            write_test_file(&c_dir, category, cat_fixtures)?;
        }

        // Make run_tests.sh executable (best-effort; ignore errors).
        let _ = std::process::Command::new("chmod")
            .args(["+x", "run_tests.sh"])
            .current_dir(&c_dir)
            .status();

        // Run clang-format if available (best-effort; ignore errors).
        let _ = std::process::Command::new("clang-format")
            .args(["-i", "-style=LLVM"])
            .args(
                std::fs::read_dir(&c_dir)
                    .into_iter()
                    .flatten()
                    .filter_map(|e| e.ok())
                    .map(|e| e.file_name().into_string().unwrap_or_default())
                    .filter(|n| n.ends_with(".c") || n.ends_with(".h"))
                    .collect::<Vec<_>>(),
            )
            .current_dir(&c_dir)
            .status();

        println!("Generated C e2e tests in {c_dir}");
        Ok(())
    }
}

// ─── File writers ─────────────────────────────────────────────────────────────

fn write_makefile(dir: &Utf8Path) -> Result<()> {
    let content = r#"# Code generated by liter-lm-e2e-generator. DO NOT EDIT.

CC      := cc
CFLAGS  := -Wall -Wextra -std=c11 -I.
LDFLAGS := -lcurl

# Path to the compiled liter-lm FFI shared library.
# Override on the command line: make LIB_DIR=/path/to/lib
LIB_DIR ?= ../../target/release
LDFLAGS += -L$(LIB_DIR) -lliter_lm_ffi -Wl,-rpath,$(LIB_DIR)

SRCS    := $(wildcard test_*.c) helpers.c
BINS    := $(patsubst %.c,%,$(filter test_%.c,$(SRCS)))

.PHONY: all clean run

all: $(BINS)

%: %.c helpers.c helpers.h
	$(CC) $(CFLAGS) -o $@ $< helpers.c $(LDFLAGS)

run: all
	@for bin in $(BINS); do \
	    echo "=== Running $$bin ==="; \
	    ./$$bin; \
	done

clean:
	rm -f $(BINS)
"#;
    std::fs::write(dir.join("Makefile"), content).map_err(|e| anyhow::anyhow!("Failed to write Makefile: {e}"))
}

