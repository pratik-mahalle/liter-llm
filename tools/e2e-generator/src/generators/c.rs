fn write_helpers_c(dir: &Utf8Path) -> Result<()> {
    let content = r#"/* Code generated by liter-lm-e2e-generator. DO NOT EDIT. */

#include "helpers.h"

#include <assert.h>
#include <stdio.h>
#include <stdlib.h>
#include <string.h>

#include <curl/curl.h>

/* ── libcurl write callback ─────────────────────────────────────────────────── */

typedef struct {
    char  *data;
    size_t len;
    size_t cap;
} Buffer;

static size_t write_callback(void *ptr, size_t size, size_t nmemb, void *userdata)
{
    Buffer *buf   = (Buffer *)userdata;
    size_t  bytes = size * nmemb;

    if (buf->len + bytes + 1 > buf->cap) {
        size_t new_cap = buf->cap ? buf->cap * 2 : 4096;
        while (new_cap < buf->len + bytes + 1)
            new_cap *= 2;
        char *p = realloc(buf->data, new_cap);
        if (!p) return 0;
        buf->data = p;
        buf->cap  = new_cap;
    }

    memcpy(buf->data + buf->len, ptr, bytes);
    buf->len += bytes;
    buf->data[buf->len] = '\0';

    return bytes;
}

/* ── HTTP helpers ────────────────────────────────────────────────────────────── */

LiterLmResponse *liter_lm_http_post(const char *url, const char *json_body)
{
    LiterLmResponse *resp = calloc(1, sizeof(*resp));
    if (!resp) return NULL;

    Buffer buf = { .data = NULL, .len = 0, .cap = 0 };

    CURL *curl = curl_easy_init();
    if (!curl) { free(resp); return NULL; }

    struct curl_slist *headers = NULL;
    headers = curl_slist_append(headers, "Content-Type: application/json");

    curl_easy_setopt(curl, CURLOPT_URL, url);
    curl_easy_setopt(curl, CURLOPT_POSTFIELDS, json_body ? json_body : "{}");
    curl_easy_setopt(curl, CURLOPT_HTTPHEADER, headers);
    curl_easy_setopt(curl, CURLOPT_WRITEFUNCTION, write_callback);
    curl_easy_setopt(curl, CURLOPT_WRITEDATA, &buf);

    curl_easy_perform(curl);
    curl_easy_getinfo(curl, CURLINFO_RESPONSE_CODE, &resp->status);

    curl_slist_free_all(headers);
    curl_easy_cleanup(curl);

    resp->body = buf.data;
    resp->len  = buf.len;
    return resp;
}

LiterLmResponse *liter_lm_http_get(const char *url)
{
    LiterLmResponse *resp = calloc(1, sizeof(*resp));
    if (!resp) return NULL;

    Buffer buf = { .data = NULL, .len = 0, .cap = 0 };

    CURL *curl = curl_easy_init();
    if (!curl) { free(resp); return NULL; }

    curl_easy_setopt(curl, CURLOPT_URL, url);
    curl_easy_setopt(curl, CURLOPT_WRITEFUNCTION, write_callback);
    curl_easy_setopt(curl, CURLOPT_WRITEDATA, &buf);

    curl_easy_perform(curl);
    curl_easy_getinfo(curl, CURLINFO_RESPONSE_CODE, &resp->status);
    curl_easy_cleanup(curl);

    resp->body = buf.data;
    resp->len  = buf.len;
    return resp;
}

/* SSE line buffer. */
typedef struct {
    char  **chunks;
    int     count;
    int     max;
    char    line_buf[65536];
    size_t  line_len;
} SseState;

static size_t sse_write_callback(void *ptr, size_t size, size_t nmemb, void *userdata)
{
    SseState *st    = (SseState *)userdata;
    size_t    bytes = size * nmemb;
    const char *src = (const char *)ptr;

    for (size_t i = 0; i < bytes; i++) {
        char c = src[i];
        if (c == '\n') {
            st->line_buf[st->line_len] = '\0';
            /* Check for "data: " prefix and skip "[DONE]". */
            if (strncmp(st->line_buf, "data: ", 6) == 0 &&
                strstr(st->line_buf, "[DONE]") == NULL &&
                st->count < st->max)
            {
                st->chunks[st->count++] = strdup(st->line_buf + 6);
            }
            st->line_len = 0;
        } else if (st->line_len + 1 < sizeof(st->line_buf)) {
            st->line_buf[st->line_len++] = c;
        }
    }
    return bytes;
}

int liter_lm_read_sse(const char *url, const char *json_body,
                      char **chunks_out, int max_chunks)
{
    SseState st;
    memset(&st, 0, sizeof(st));
    st.chunks = chunks_out;
    st.max    = max_chunks;

    CURL *curl = curl_easy_init();
    if (!curl) return 0;

    struct curl_slist *headers = NULL;
    headers = curl_slist_append(headers, "Content-Type: application/json");

    curl_easy_setopt(curl, CURLOPT_URL, url);
    curl_easy_setopt(curl, CURLOPT_POSTFIELDS, json_body ? json_body : "{}");
    curl_easy_setopt(curl, CURLOPT_HTTPHEADER, headers);
    curl_easy_setopt(curl, CURLOPT_WRITEFUNCTION, sse_write_callback);
    curl_easy_setopt(curl, CURLOPT_WRITEDATA, &st);

    curl_easy_perform(curl);
    curl_slist_free_all(headers);
    curl_easy_cleanup(curl);

    return st.count;
}

void liter_lm_response_free(LiterLmResponse *resp)
{
    if (!resp) return;
    free(resp->body);
    free(resp);
}

/* ── Assertion helpers ───────────────────────────────────────────────────────── */

void liter_lm_assert_status(const LiterLmResponse *resp, long expected,
                            const char *test_name)
{
    if (resp->status != expected) {
        fprintf(stderr, "FAIL [%s]: expected status %ld, got %ld\n",
                test_name, expected, resp->status);
        abort();
    }
}

/*
 * Minimal JSON field assertion: search for "key":"value" or "key":value in json.
 * This is intentionally simple — not a full JSON parser.
 */
void liter_lm_assert_json_field(const char *json, const char *key,
                                const char *expected_value,
                                const char *test_name)
{
    if (!json || !key || !expected_value) return;

    /* Build patterns: "key":"expected" and "key": "expected" */
    char pattern1[512], pattern2[512];
    snprintf(pattern1, sizeof(pattern1), "\"%s\":\"%s\"", key, expected_value);
    snprintf(pattern2, sizeof(pattern2), "\"%s\": \"%s\"", key, expected_value);

    if (strstr(json, pattern1) == NULL && strstr(json, pattern2) == NULL) {
        fprintf(stderr,
                "FAIL [%s]: expected JSON field \"%s\" == \"%s\" not found\n"
                "  body: %.200s\n",
                test_name, key, expected_value, json);
        abort();
    }
}

void liter_lm_assert_json_array_len(const char *json, const char *key,
                                    int expected_len, const char *test_name)
{
    if (!json || !key) return;

    /* Find the key and count array elements by counting commas + 1. */
    char search[256];
    snprintf(search, sizeof(search), "\"%s\":[", key);
    const char *arr_start = strstr(json, search);
    if (!arr_start) {
        /* Try with space: "key": [ */
        snprintf(search, sizeof(search), "\"%s\": [", key);
        arr_start = strstr(json, search);
    }

    if (!arr_start) {
        fprintf(stderr,
                "FAIL [%s]: array key \"%s\" not found in JSON\n"
                "  body: %.200s\n",
                test_name, key, json);
        abort();
    }

    /* Scan to the opening '['. */
    const char *p = strchr(arr_start, '[');
    if (!p) { abort(); }
    p++;

    /* Empty array. */
    if (*p == ']') {
        if (expected_len != 0) {
            fprintf(stderr, "FAIL [%s]: expected array \"%s\" length %d, got 0\n",
                    test_name, key, expected_len);
            abort();
        }
        return;
    }

    /* Count elements: depth-aware comma counting. */
    int depth = 1, count = 1;
    for (; *p && depth > 0; p++) {
        if (*p == '[' || *p == '{') depth++;
        else if (*p == ']' || *p == '}') {
            depth--;
            if (depth == 0) break;
        } else if (*p == ',' && depth == 1) {
            count++;
        }
    }

    if (count != expected_len) {
        fprintf(stderr,
                "FAIL [%s]: expected array \"%s\" length %d, got %d\n",
                test_name, key, expected_len, count);
        abort();
    }
}
"#;
    std::fs::write(dir.join("helpers.c"), content).map_err(|e| anyhow::anyhow!("Failed to write helpers.c: {e}"))
}

