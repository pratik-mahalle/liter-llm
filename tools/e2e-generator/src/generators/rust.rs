use std::fmt::Write as FmtWrite;

use anyhow::Result;
use camino::Utf8Path;

use crate::fixture::{Fixture, group_by_category};
use crate::generators::Generator;

pub struct RustGenerator;

impl Generator for RustGenerator {
    fn generate(&self, fixtures: &[Fixture], output_root: &Utf8Path) -> Result<()> {
        let rust_dir = output_root.join("rust");

        // Clean stale output before regenerating.
        let _ = std::fs::remove_dir_all(&rust_dir);

        std::fs::create_dir_all(rust_dir.join("src"))
            .map_err(|e| anyhow::anyhow!("Failed to create rust/src dir: {e}"))?;
        std::fs::create_dir_all(rust_dir.join("tests"))
            .map_err(|e| anyhow::anyhow!("Failed to create rust/tests dir: {e}"))?;

        write_cargo_toml(&rust_dir)?;
        write_lib_rs(&rust_dir)?;
        // mock_server.rs lives in tests/ so each integration test file can use
        // `#[path = "mock_server.rs"] mod mock_server;`
        write_mock_server_to_tests(&rust_dir)?;

        let groups = group_by_category(fixtures);
        for (category, cat_fixtures) in &groups {
            write_test_file(&rust_dir, category, cat_fixtures)?;
        }

        // Run cargo fmt if available (best-effort; ignore errors).
        let _ = std::process::Command::new("cargo")
            .args(["fmt", "--manifest-path"])
            .arg(rust_dir.join("Cargo.toml").as_str())
            .status();

        println!("Generated Rust e2e tests in {rust_dir}");
        Ok(())
    }
}

