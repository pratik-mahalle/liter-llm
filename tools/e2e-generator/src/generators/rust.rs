// ─── Assertion helpers ────────────────────────────────────────────────────────

fn emit_chat_assertions(out: &mut String, fixture: &Fixture) {
    let assertions = &fixture.assertions;

    // choices count derived from mock response body (more reliable than assertion fields).
    if let Some(choices_arr) = fixture.api.mock_response.body.get("choices").and_then(|v| v.as_array()) {
        let count = choices_arr.len();
        writeln!(
            out,
            "    assert_eq!(response.choices.len(), {count}, \"Choices count mismatch\");"
        )
        .unwrap();

        if let Some(first_choice) = choices_arr.first() {
            // finish_reason
            if let Some(fr) = first_choice
                .get("finish_reason")
                .and_then(|v| v.as_str())
                .filter(|s| !s.is_empty())
            {
                let variant = finish_reason_variant(fr);
                writeln!(
                    out,
                    "    assert_eq!(response.choices[0].finish_reason, Some({variant}), \"finish_reason mismatch\");"
                )
                .unwrap();
            }

            // message content
            if let Some(content) = first_choice
                .get("message")
                .and_then(|m| m.get("content"))
                .and_then(|c| c.as_str())
            {
                writeln!(
                    out,
                    "    assert_eq!(response.choices[0].message.content.as_deref(), Some({content:?}), \"message content mismatch\");"
                )
                .unwrap();
            }

            // tool_calls presence
            if first_choice
                .get("message")
                .and_then(|m| m.get("tool_calls"))
                .and_then(|t| t.as_array())
                .is_some_and(|arr| !arr.is_empty())
            {
                writeln!(
                    out,
                    "    assert!(response.choices[0].message.tool_calls.as_ref().is_some_and(|tc| !tc.is_empty()), \"Expected tool_calls\");"
                )
                .unwrap();

                // First tool call function name.
                if let Some(fn_name) = first_choice
                    .get("message")
                    .and_then(|m| m.get("tool_calls"))
                    .and_then(|t| t.as_array())
                    .and_then(|arr| arr.first())
                    .and_then(|tc| tc.get("function"))
                    .and_then(|f| f.get("name"))
                    .and_then(|n| n.as_str())
                {
                    writeln!(
                        out,
                        "    assert_eq!(response.choices[0].message.tool_calls.as_ref().unwrap()[0].function.name, {fn_name:?}, \"tool call function name mismatch\");"
                    )
                    .unwrap();
                }
            }
        }
    }

    // usage
    if let Some(usage) = fixture.api.mock_response.body.get("usage").filter(|v| !v.is_null()) {
        writeln!(out, "    assert!(response.usage.is_some(), \"Expected usage object\");").unwrap();
        if let Some(total) = usage.get("total_tokens").and_then(|v| v.as_u64()) {
            writeln!(
                out,
                "    assert_eq!(response.usage.as_ref().unwrap().total_tokens, {total}, \"total_tokens mismatch\");"
            )
            .unwrap();
        }
    }

    // model
    if let Some(model) = assertions
        .model
        .as_deref()
        .or_else(|| fixture.api.mock_response.body.get("model").and_then(|v| v.as_str()))
    {
        writeln!(out, "    assert_eq!(response.model, {model:?}, \"model mismatch\");").unwrap();
    }
}

fn emit_error_assertions(out: &mut String, fixture: &Fixture) {
    let status = fixture.api.mock_response.status;
    match status {
        401 => writeln!(
            out,
            "    assert!(matches!(result.unwrap_err(), liter_lm::LiterLmError::Authentication {{ .. }}), \"Expected Authentication error\");"
        )
        .unwrap(),
        429 => writeln!(
            out,
            "    assert!(matches!(result.unwrap_err(), liter_lm::LiterLmError::RateLimited {{ .. }}), \"Expected RateLimited error\");"
        )
        .unwrap(),
        400 => writeln!(
            out,
            "    assert!(matches!(result.unwrap_err(), liter_lm::LiterLmError::BadRequest {{ .. }} | liter_lm::LiterLmError::ContextWindowExceeded {{ .. }} | liter_lm::LiterLmError::ContentPolicy {{ .. }}), \"Expected 400-class error\");"
        )
        .unwrap(),
        404 => writeln!(
            out,
            "    assert!(matches!(result.unwrap_err(), liter_lm::LiterLmError::NotFound {{ .. }}), \"Expected NotFound error\");"
        )
        .unwrap(),
        500 => writeln!(
            out,
            "    assert!(matches!(result.unwrap_err(), liter_lm::LiterLmError::ServerError {{ .. }}), \"Expected ServerError error\");"
        )
        .unwrap(),
        502 | 503 => writeln!(
            out,
            "    assert!(matches!(result.unwrap_err(), liter_lm::LiterLmError::ServiceUnavailable {{ .. }}), \"Expected ServiceUnavailable error\");"
        )
        .unwrap(),
        _ => writeln!(out, "    let _ = result.unwrap_err();").unwrap(),
    }
}

// ─── Helpers ──────────────────────────────────────────────────────────────────

/// Return the path component for the given API method.
///
/// The `DefaultClient` constructs the full URL as `base_url + path`.  When we
/// set `base_url` to `http://127.0.0.1:PORT` (no trailing `/v1`), the provider
/// appends just the path below, so the mock server must match those paths.
fn endpoint_for_method(method: &str) -> &'static str {
    match method {
        "chat" | "chat_stream" => "/chat/completions",
        "embed" => "/embeddings",
        "list_models" => "/models",
        _ => "/chat/completions",
    }
}

fn finish_reason_variant(fr: &str) -> &'static str {
    match fr {
        "stop" => "liter_lm::FinishReason::Stop",
        "length" => "liter_lm::FinishReason::Length",
        "tool_calls" => "liter_lm::FinishReason::ToolCalls",
        "content_filter" => "liter_lm::FinishReason::ContentFilter",
        "function_call" => "liter_lm::FinishReason::FunctionCall",
        _ => "liter_lm::FinishReason::Stop",
    }
}

/// Convert a category or fixture id to a valid Rust identifier.
fn sanitize_name(name: &str) -> String {
    name.chars()
        .map(|c| if c.is_alphanumeric() || c == '_' { c } else { '_' })
        .collect::<String>()
        .to_lowercase()
}
