fn write_mock_server_to_tests(dir: &Utf8Path) -> Result<()> {
    let content = r#"// Code generated by liter-lm-e2e-generator. DO NOT EDIT.
//
// Minimal axum-based mock HTTP server for liter-lm e2e tests.

use std::net::SocketAddr;
use std::sync::Arc;

use axum::Router;
use axum::body::Body;
use axum::extract::State;
use axum::http::{Request, StatusCode};
use axum::response::{IntoResponse, Response};
use tokio::net::TcpListener;

/// A single mock route: match by path + method, return a configured response.
#[derive(Clone, Debug)]
pub struct MockRoute {
    /// URL path to match, e.g. `"/v1/chat/completions"`.
    pub path: &'static str,
    /// HTTP method to match, e.g. `"POST"` or `"GET"`.
    pub method: &'static str,
    /// HTTP status code to return.
    pub status: u16,
    /// Response body JSON string (used when `stream_chunks` is empty).
    pub body: String,
    /// Ordered SSE data payloads for streaming responses.
    /// Each entry becomes `data: <chunk>\n\n` in the response.
    /// A final `data: [DONE]\n\n` is always appended.
    pub stream_chunks: Vec<String>,
}

struct ServerState {
    routes: Vec<MockRoute>,
}

pub struct MockServer {
    /// Base URL of the mock server, e.g. `"http://127.0.0.1:54321"`.
    pub url: String,
    handle: tokio::task::JoinHandle<()>,
}

impl MockServer {
    /// Start a mock server with the given routes.  Binds to a random port on
    /// localhost and returns immediately once the server is listening.
    pub async fn start(routes: Vec<MockRoute>) -> Self {
        let state = Arc::new(ServerState { routes });

        let app = Router::new()
            .fallback(handle_request)
            .with_state(state);

        let listener = TcpListener::bind("127.0.0.1:0")
            .await
            .expect("Failed to bind mock server port");
        let addr: SocketAddr = listener.local_addr().expect("Failed to get local addr");
        let url = format!("http://{addr}");

        let handle = tokio::spawn(async move {
            axum::serve(listener, app)
                .await
                .expect("Mock server failed");
        });

        MockServer { url, handle }
    }

    /// Stop the mock server.
    pub fn shutdown(self) {
        self.handle.abort();
    }
}

async fn handle_request(
    State(state): State<Arc<ServerState>>,
    req: Request<Body>,
) -> Response {
    let path = req.uri().path().to_owned();
    let method = req.method().as_str().to_uppercase();

    for route in &state.routes {
        if route.path == path && route.method.to_uppercase() == method {
            let status = StatusCode::from_u16(route.status).unwrap_or(StatusCode::INTERNAL_SERVER_ERROR);

            if !route.stream_chunks.is_empty() {
                // Build SSE body: data: <chunk>\n\n ... data: [DONE]\n\n
                let mut sse = String::new();
                for chunk in &route.stream_chunks {
                    sse.push_str("data: ");
                    sse.push_str(chunk);
                    sse.push_str("\n\n");
                }
                sse.push_str("data: [DONE]\n\n");

                return Response::builder()
                    .status(status)
                    .header("content-type", "text/event-stream")
                    .header("cache-control", "no-cache")
                    .body(Body::from(sse))
                    .unwrap()
                    .into_response();
            }

            return Response::builder()
                .status(status)
                .header("content-type", "application/json")
                .body(Body::from(route.body.clone()))
                .unwrap()
                .into_response();
        }
    }

    // No matching route → 404.
    Response::builder()
        .status(StatusCode::NOT_FOUND)
        .body(Body::from(format!("No mock route for {method} {path}")))
        .unwrap()
        .into_response()
}
"#;
    std::fs::write(dir.join("tests").join("mock_server.rs"), content)
        .map_err(|e| anyhow::anyhow!("Failed to write tests/mock_server.rs: {e}"))
}

// ─── Test file generation ─────────────────────────────────────────────────────

fn write_test_file(dir: &Utf8Path, category: &str, fixtures: &[&Fixture]) -> Result<()> {
    let mut out = String::new();

    writeln!(out, "// Code generated by liter-lm-e2e-generator. DO NOT EDIT.").unwrap();
    writeln!(out, "#![allow(clippy::too_many_lines)]").unwrap();
    writeln!(out).unwrap();
    // Rust integration tests do not support plain `mod foo;` to refer to sibling
    // files in tests/.  The `#[path]` attribute loads the file explicitly.
    writeln!(out, "#[path = \"mock_server.rs\"]").unwrap();
    writeln!(out, "mod mock_server;").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "use liter_lm::{{ClientConfigBuilder, DefaultClient, LlmClient}};").unwrap();
    writeln!(out).unwrap();

    for fixture in fixtures {
        // Skip fixtures targeting other languages.
        if fixture.skip.languages.iter().any(|l| l == "rust") {
            continue;
        }

        write_test_fn(&mut out, fixture);
    }

    // Trim trailing blank lines.
    let out = out.trim_end().to_string() + "\n";

    let filename = format!("test_{}.rs", sanitize_name(category));
    let path = dir.join("tests").join(&filename);
    std::fs::write(&path, &out).map_err(|e| anyhow::anyhow!("Failed to write {filename}: {e}"))
}

/// Emit a single `#[tokio::test]` function for the fixture.
fn write_test_fn(out: &mut String, fixture: &Fixture) {
    let fn_name = sanitize_name(&fixture.id);

    writeln!(out, "/// {}", fixture.description).unwrap();
    writeln!(out, "#[tokio::test]").unwrap();
    writeln!(out, "async fn {fn_name}() {{").unwrap();

    let method = fixture.api.method.as_str();

    // Determine the endpoint path for the mock route.
    let endpoint = endpoint_for_method(method);

    // Build mock route literal.
    emit_mock_server_start(out, fixture, endpoint, method);

    // Build client.
    writeln!(out).unwrap();
    writeln!(
        out,
        "    let config = ClientConfigBuilder::new(\"test-key\")\
\n        .base_url(&server.url)\
\n        .max_retries(0)\
\n        .build();"
    )
    .unwrap();
    writeln!(out, "    let client = DefaultClient::new(config, None).unwrap();").unwrap();
    writeln!(out).unwrap();

    // Emit the API call + assertions.
    match method {
        "chat" => emit_chat_test(out, fixture),
        "chat_stream" => emit_stream_test(out, fixture),
        "embed" => emit_embed_test(out, fixture),
        "list_models" => emit_list_models_test(out, fixture),
        other => {
            writeln!(out, "    // TODO: unknown method {other:?}").unwrap();
        }
    }

    writeln!(out).unwrap();
    writeln!(out, "    server.shutdown();").unwrap();
    writeln!(out, "}}").unwrap();
    writeln!(out).unwrap();
}

// ─── Mock server start emission ───────────────────────────────────────────────

fn emit_mock_server_start(out: &mut String, fixture: &Fixture, endpoint: &str, method: &str) {
    let http_method = if method == "list_models" { "GET" } else { "POST" };
    let status = fixture.api.mock_response.status;

    writeln!(out, "    let server = mock_server::MockServer::start(vec![").unwrap();
    writeln!(out, "        mock_server::MockRoute {{").unwrap();
    writeln!(out, "            path: {endpoint:?},").unwrap();
    writeln!(out, "            method: {http_method:?},").unwrap();
    writeln!(out, "            status: {status},").unwrap();

    // body
    let body_json = serde_json::to_string(&fixture.api.mock_response.body).unwrap_or_default();
    writeln!(out, "            body: r#\"{}\"#.to_string(),", body_json).unwrap();

    // stream_chunks
    if fixture.api.mock_response.stream_chunks.is_empty() {
        writeln!(out, "            stream_chunks: vec![],").unwrap();
    } else {
        writeln!(out, "            stream_chunks: vec![").unwrap();
        for chunk in &fixture.api.mock_response.stream_chunks {
            let chunk_json = serde_json::to_string(chunk).unwrap_or_default();
            writeln!(out, "                r#\"{chunk_json}\"#.to_string(),").unwrap();
        }
        writeln!(out, "            ],").unwrap();
    }

    writeln!(out, "        }},").unwrap();
    writeln!(out, "    ]).await;").unwrap();
}

// ─── API call + assertion emission ────────────────────────────────────────────

fn emit_chat_test(out: &mut String, fixture: &Fixture) {
    // Build the request from the fixture's request JSON.
    let req_json = serde_json::to_string(&fixture.api.request).unwrap_or_default();
    writeln!(out, "    // Build request from fixture JSON.").unwrap();
    writeln!(
        out,
        "    let req: liter_lm::ChatCompletionRequest = serde_json::from_str(r#\"{req_json}\"#).unwrap();"
    )
    .unwrap();
    writeln!(out).unwrap();

    // An error test is one where the mock response is a non-2xx status or the
    // fixture explicitly marks expect_success = false.
    let is_error = fixture.api.mock_response.status >= 400 || !fixture.assertions.expect_success;

    if is_error {
        writeln!(out, "    let result = client.chat(req).await;").unwrap();
        writeln!(out, "    assert!(result.is_err(), \"Expected error but got Ok\");").unwrap();
        emit_error_assertions(out, fixture);
    } else {
        writeln!(
            out,
            "    let response = client.chat(req).await.expect(\"chat call failed\");"
        )
        .unwrap();
        emit_chat_assertions(out, fixture);
    }
}

fn emit_stream_test(out: &mut String, fixture: &Fixture) {
    let req_json = serde_json::to_string(&fixture.api.request).unwrap_or_default();
    writeln!(
        out,
        "    let req: liter_lm::ChatCompletionRequest = serde_json::from_str(r#\"{req_json}\"#).unwrap();"
    )
    .unwrap();
    writeln!(out).unwrap();
    writeln!(
        out,
        "    let stream = client.chat_stream(req).await.expect(\"chat_stream call failed\");"
    )
    .unwrap();
    writeln!(out).unwrap();
    writeln!(out, "    use tokio_stream::StreamExt as _;").unwrap();
    writeln!(out, "    let chunks: Vec<_> = stream.collect::<Vec<_>>().await;").unwrap();
    writeln!(
        out,
        "    let ok_chunks: Vec<_> = chunks.iter().filter_map(|c| c.as_ref().ok()).collect();"
    )
    .unwrap();

    let assertions = &fixture.assertions;

    if let Some(min_chunks) = assertions.stream_chunk_count_min {
        writeln!(
            out,
            "    assert!(ok_chunks.len() >= {min_chunks}, \"Expected to receive at least {min_chunks} stream chunk(s), got {{}}\", ok_chunks.len());"
        )
        .unwrap();
    } else {
        // Default: assert we received at least one chunk.
        writeln!(
            out,
            "    assert!(!ok_chunks.is_empty(), \"Expected to receive at least 1 stream chunk\");"
        )
        .unwrap();
    }

    // Collect concatenated content for content assertions.
    let needs_content = fixture.api.mock_response.stream_chunks.iter().any(|c| {
        c.get("choices")
            .and_then(|ch| ch.as_array())
            .and_then(|arr| arr.first())
            .and_then(|ch| ch.get("delta"))
            .and_then(|d| d.get("content"))
            .and_then(|c| c.as_str())
            .is_some_and(|s| !s.is_empty())
    });

    if needs_content {
        writeln!(out, "    let content: String = ok_chunks.iter()").unwrap();
        writeln!(out, "        .flat_map(|c| c.choices.iter())").unwrap();
        writeln!(out, "        .filter_map(|ch| ch.delta.content.as_deref())").unwrap();
        writeln!(out, "        .collect();").unwrap();
    }

    // stream_chunk_count_min (stored in the raw assertions JSON we don't have a typed field for;
    // but we can infer from stream_chunks length in the mock response)
    let chunk_count = fixture.api.mock_response.stream_chunks.len();
    if chunk_count > 0 {
        // Check that we received at least the number of meaningful (non-empty) chunks.
        let meaningful: usize = fixture
            .api
            .mock_response
            .stream_chunks
            .iter()
            .filter(|c| {
                c.get("choices")
                    .and_then(|ch| ch.as_array())
                    .and_then(|arr| arr.first())
                    .and_then(|ch| ch.get("delta"))
                    .and_then(|d| d.get("content"))
                    .and_then(|v| v.as_str())
                    .is_some_and(|s| !s.is_empty())
            })
            .count();

        if meaningful > 0 {
            writeln!(
                out,
                "    assert!(ok_chunks.len() >= {meaningful}, \"Expected at least {meaningful} chunk(s), got {{}}\", ok_chunks.len());"
            )
            .unwrap();
        } else {
            writeln!(
                out,
                "    assert!(!ok_chunks.is_empty(), \"Expected at least one stream chunk\");"
            )
            .unwrap();
        }
    }

    // Concatenated content assertion derived from stream_chunks.
    if needs_content {
        let expected_content: String = fixture
            .api
            .mock_response
            .stream_chunks
            .iter()
            .filter_map(|c| {
                c.get("choices")
                    .and_then(|ch| ch.as_array())
                    .and_then(|arr| arr.first())
                    .and_then(|ch| ch.get("delta"))
                    .and_then(|d| d.get("content"))
                    .and_then(|v| v.as_str())
                    .map(str::to_owned)
            })
            .collect();

        if !expected_content.is_empty() {
            writeln!(
                out,
                "    assert_eq!(content, {:?}, \"Stream content mismatch\");",
                expected_content
            )
            .unwrap();
        }
    }
}

