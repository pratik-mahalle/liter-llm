fn endpoint_for_method(method: &str) -> &'static str {
    match method {
        "chat" | "chat_stream" => "/chat/completions",
        "embed" => "/embeddings",
        "list_models" => "/models",
        "image_generate" => "/images/generations",
        "speech" => "/audio/speech",
        "transcribe" => "/audio/transcriptions",
        "moderate" => "/moderations",
        "rerank" => "/rerank",
        "create_file" | "list_files" => "/files",
        "retrieve_file" | "delete_file" => "/files/file-placeholder",
        "file_content" => "/files/file-placeholder/content",
        "create_batch" | "list_batches" => "/batches",
        "retrieve_batch" | "cancel_batch" => "/batches/batch-placeholder",
        "create_response" => "/responses",
        "retrieve_response" | "cancel_response" => "/responses/resp-placeholder",
        _ => "/chat/completions",
    }
}

/// Encode a string as a Go raw string literal (backtick) when possible,
/// falling back to a regular double-quoted string.
fn go_raw_string(s: &str) -> String {
    if s.contains('`') {
        // Can't use raw string; escape double quotes instead.
        format!("\"{}\"", s.replace('\\', "\\\\").replace('"', "\\\""))
    } else {
        format!("`{s}`")
    }
}

/// Convert a hyphenated/underscored category name to PascalCase for Go test function names.
fn pascal_case(s: &str) -> String {
    s.split(['-', '_', ' '])
        .filter(|p| !p.is_empty())
        .map(|part| {
            let mut chars = part.chars();
            match chars.next() {
                None => String::new(),
                Some(first) => first.to_uppercase().collect::<String>() + chars.as_str(),
            }
        })
        .collect()
}

fn sanitize_name(name: &str) -> String {
    name.chars()
        .map(|c| if c.is_alphanumeric() || c == '_' { c } else { '_' })
        .collect::<String>()
        .to_lowercase()
}
