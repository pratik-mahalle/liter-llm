fn write_test_file(dir: &Utf8Path, category: &str, fixtures: &[&Fixture]) -> Result<()> {
    let mut out = String::new();

    // Determine which imports are actually needed by the active fixtures.
    let active: Vec<&&Fixture> = fixtures
        .iter()
        .filter(|f| !f.skip.languages.iter().any(|l| l == "go"))
        .collect();

    let needs_bytes = active.iter().any(|f| f.api.method != "list_models");
    let needs_json = active.iter().any(|f| {
        let is_error = f.api.mock_response.status >= 400 || !f.assertions.expect_success;
        if is_error {
            return false;
        }
        if f.api.method == "chat_stream" {
            // json.Unmarshal is used only when there's non-empty stream content.
            let has_content = f.api.mock_response.stream_chunks.iter().any(|c| {
                c.get("choices")
                    .and_then(|ch| ch.as_array())
                    .and_then(|arr| arr.first())
                    .and_then(|ch| ch.get("delta"))
                    .and_then(|d| d.get("content"))
                    .and_then(|v| v.as_str())
                    .is_some_and(|s| !s.is_empty())
            });
            return has_content;
        }
        true
    });
    let needs_fmt = active
        .iter()
        .any(|f| f.api.mock_response.status >= 400 || !f.assertions.expect_success);

    writeln!(out, "// Code generated by liter-lm-e2e-generator. DO NOT EDIT.").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "package literlm_e2e").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "import (").unwrap();
    if needs_bytes {
        writeln!(out, "\t\"bytes\"").unwrap();
    }
    if needs_json {
        writeln!(out, "\t\"encoding/json\"").unwrap();
    }
    if needs_fmt {
        writeln!(out, "\t\"fmt\"").unwrap();
    }
    writeln!(out, "\t\"net/http\"").unwrap();
    writeln!(out, "\t\"testing\"").unwrap();
    writeln!(out, ")").unwrap();
    writeln!(out).unwrap();

    let func_name = format!("Test{}", pascal_case(category));
    writeln!(out, "func {func_name}(t *testing.T) {{").unwrap();

    for fixture in fixtures {
        if fixture.skip.languages.iter().any(|l| l == "go") {
            continue;
        }
        write_subtest(&mut out, fixture);
    }

    writeln!(out, "}}").unwrap();

    let out = out.trim_end().to_string() + "\n";
    let filename = format!("{}_test.go", sanitize_name(category));
    let path = dir.join(&filename);
    std::fs::write(&path, &out).map_err(|e| anyhow::anyhow!("Failed to write {filename}: {e}"))
}

fn write_subtest(out: &mut String, fixture: &Fixture) {
    let sub_name = sanitize_name(&fixture.id);
    let method = fixture.api.method.as_str();
    let endpoint = endpoint_for_method(method);
    let http_method = match method {
        "list_models" | "retrieve_file" | "list_files" | "file_content" | "retrieve_batch" | "list_batches"
        | "retrieve_response" => "GET",
        "delete_file" | "cancel_batch" | "cancel_response" => "POST",
        _ => "POST",
    };
    let status = fixture.api.mock_response.status;

    writeln!(out, "\tt.Run({:?}, func(t *testing.T) {{", fixture.id).unwrap();
    writeln!(out, "\t\t// {}", fixture.description).unwrap();

    // Build mock server routes.
    let body_json = serde_json::to_string(&fixture.api.mock_response.body).unwrap_or_default();
    let escaped_body = go_raw_string(&body_json);

    writeln!(out, "\t\tserver := NewMockServer([]MockRoute{{").unwrap();
    writeln!(out, "\t\t\t{{").unwrap();
    writeln!(out, "\t\t\t\tPath:   {:?},", endpoint).unwrap();
    writeln!(out, "\t\t\t\tMethod: {:?},", http_method).unwrap();
    writeln!(out, "\t\t\t\tStatus: {status},").unwrap();
    writeln!(out, "\t\t\t\tBody:   {escaped_body},").unwrap();

    if fixture.api.mock_response.stream_chunks.is_empty() {
        writeln!(out, "\t\t\t\tStreamChunks: nil,").unwrap();
    } else {
        writeln!(out, "\t\t\t\tStreamChunks: []string{{").unwrap();
        for chunk in &fixture.api.mock_response.stream_chunks {
            let chunk_json = serde_json::to_string(chunk).unwrap_or_default();
            writeln!(out, "\t\t\t\t\t{},", go_raw_string(&chunk_json)).unwrap();
        }
        writeln!(out, "\t\t\t\t}},").unwrap();
    }

    writeln!(out, "\t\t\t}},").unwrap();
    writeln!(out, "\t\t}})").unwrap();
    writeln!(out, "\t\tdefer server.Close()").unwrap();
    writeln!(out).unwrap();

    // Build client and call the appropriate API method.
    match method {
        "chat" => emit_go_chat_test(out, fixture, endpoint, &sub_name),
        "chat_stream" => emit_go_stream_test(out, fixture, endpoint),
        "embed" => emit_go_embed_test(out, fixture, endpoint),
        "list_models" => emit_go_list_models_test(out, fixture, endpoint),
        other => {
            writeln!(out, "\t\t// TODO: unknown method {other:?}").unwrap();
        }
    }

    writeln!(out, "\t}})").unwrap();
    writeln!(out).unwrap();
}

