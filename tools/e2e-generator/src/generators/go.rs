fn emit_go_chat_assertions(out: &mut String, fixture: &Fixture) {
    if let Some(choices_arr) = fixture.api.mock_response.body.get("choices").and_then(|v| v.as_array()) {
        let count = choices_arr.len();
        writeln!(out, "\t\tchoices, _ := result[\"choices\"].([]interface{{}})").unwrap();
        writeln!(out, "\t\tAssertEqual(t, \"choices count\", {count}, len(choices))").unwrap();

        if let Some(first_choice) = choices_arr.first()
            && let Some(content) = first_choice
                .get("message")
                .and_then(|m| m.get("content"))
                .and_then(|c| c.as_str())
        {
            writeln!(out).unwrap();
            writeln!(out, "\t\tif len(choices) > 0 {{").unwrap();
            writeln!(out, "\t\t\tchoice, _ := choices[0].(map[string]interface{{}})").unwrap();
            writeln!(
                out,
                "\t\t\tmessage, _ := choice[\"message\"].(map[string]interface{{}})"
            )
            .unwrap();
            writeln!(
                out,
                "\t\t\tAssertEqual(t, \"first choice content\", {:?}, message[\"content\"])",
                content
            )
            .unwrap();
            writeln!(out, "\t\t}}").unwrap();
        }
    }

    if let Some(model) = fixture.api.mock_response.body.get("model").and_then(|v| v.as_str()) {
        writeln!(out, "\t\tAssertEqual(t, \"model\", {:?}, result[\"model\"])", model).unwrap();
    }

    if let Some(usage) = fixture.api.mock_response.body.get("usage").filter(|v| !v.is_null()) {
        writeln!(out).unwrap();
        writeln!(out, "\t\tusage, _ := result[\"usage\"].(map[string]interface{{}})").unwrap();
        writeln!(out, "\t\tAssertTrue(t, \"usage not nil\", usage != nil)").unwrap();
        if let Some(total) = usage.get("total_tokens").and_then(|v| v.as_f64()) {
            writeln!(
                out,
                "\t\tAssertEqual(t, \"total_tokens\", float64({total}), usage[\"total_tokens\"])"
            )
            .unwrap();
        }
    }
}

fn emit_go_status_assertion(out: &mut String, status: u16) {
    writeln!(
        out,
        "\t\tAssertEqual(t, \"error status code\", {status}, resp.StatusCode)"
    )
    .unwrap();
    // Suppress the fmt import warning if we don't use it.
    writeln!(out, "\t\t_ = fmt.Sprintf(\"\") // suppress import").unwrap();
}

