fn emit_go_stream_test(out: &mut String, fixture: &Fixture, endpoint: &str) {
    let req_json = serde_json::to_string(&fixture.api.request).unwrap_or_default();
    let is_error = fixture.api.mock_response.status >= 400 || !fixture.assertions.expect_success;

    writeln!(out, "\t\treqBody := bytes.NewBufferString({:?})", req_json).unwrap();
    writeln!(
        out,
        "\t\tresp, err := http.Post(server.URL+{:?}, \"application/json\", reqBody)",
        endpoint
    )
    .unwrap();
    writeln!(out, "\t\tif err != nil {{").unwrap();
    writeln!(out, "\t\t\tt.Fatalf(\"http.Post failed: %v\", err)").unwrap();
    writeln!(out, "\t\t}}").unwrap();
    writeln!(out, "\t\tdefer resp.Body.Close()").unwrap();
    writeln!(out).unwrap();

    if is_error {
        writeln!(
            out,
            "\t\tAssertTrue(t, \"expected error status\", resp.StatusCode >= 400)"
        )
        .unwrap();
        emit_go_status_assertion(out, fixture.api.mock_response.status);
        return;
    }

    writeln!(
        out,
        "\t\tAssertEqual(t, \"HTTP status code\", {}, resp.StatusCode)",
        fixture.api.mock_response.status
    )
    .unwrap();
    writeln!(out).unwrap();

    writeln!(out, "\t\tchunks, err := ReadSSEChunks(resp.Body)").unwrap();
    writeln!(out, "\t\tif err != nil {{").unwrap();
    writeln!(out, "\t\t\tt.Fatalf(\"ReadSSEChunks: %v\", err)").unwrap();
    writeln!(out, "\t\t}}").unwrap();
    writeln!(out).unwrap();

    let meaningful: usize = fixture
        .api
        .mock_response
        .stream_chunks
        .iter()
        .filter(|c| {
            c.get("choices")
                .and_then(|ch| ch.as_array())
                .and_then(|arr| arr.first())
                .and_then(|ch| ch.get("delta"))
                .and_then(|d| d.get("content"))
                .and_then(|v| v.as_str())
                .is_some_and(|s| !s.is_empty())
        })
        .count();

    if fixture.api.mock_response.stream_chunks.is_empty() {
        writeln!(
            out,
            "\t\tAssertTrue(t, \"expected 0 chunks for empty stream\", len(chunks) == 0)"
        )
        .unwrap();
    } else {
        let min_chunks = meaningful.max(1);
        writeln!(
            out,
            "\t\tAssertTrue(t, \"expected at least {min_chunks} chunk(s)\", len(chunks) >= {min_chunks})"
        )
        .unwrap();
    }

    // Collect expected content from stream chunks.
    let expected_content: String = fixture
        .api
        .mock_response
        .stream_chunks
        .iter()
        .filter_map(|c| {
            c.get("choices")
                .and_then(|ch| ch.as_array())
                .and_then(|arr| arr.first())
                .and_then(|ch| ch.get("delta"))
                .and_then(|d| d.get("content"))
                .and_then(|v| v.as_str())
                .map(str::to_owned)
        })
        .collect();

    if !expected_content.is_empty() {
        writeln!(out).unwrap();
        writeln!(out, "\t\t// Verify each chunk is valid JSON and reconstruct content.").unwrap();
        writeln!(out, "\t\tvar content string").unwrap();
        writeln!(out, "\t\tfor _, rawChunk := range chunks {{").unwrap();
        writeln!(out, "\t\t\tvar chunk map[string]interface{{}}").unwrap();
        writeln!(
            out,
            "\t\t\tif err := json.Unmarshal([]byte(rawChunk), &chunk); err != nil {{"
        )
        .unwrap();
        writeln!(out, "\t\t\t\tcontinue // some chunks may not decode cleanly").unwrap();
        writeln!(out, "\t\t\t}}").unwrap();
        writeln!(
            out,
            "\t\t\tif choices, ok := chunk[\"choices\"].([]interface{{}}); ok && len(choices) > 0 {{"
        )
        .unwrap();
        writeln!(
            out,
            "\t\t\t\tif choice, ok := choices[0].(map[string]interface{{}}); ok {{"
        )
        .unwrap();
        writeln!(
            out,
            "\t\t\t\t\tif delta, ok := choice[\"delta\"].(map[string]interface{{}}); ok {{"
        )
        .unwrap();
        writeln!(out, "\t\t\t\t\t\tif c, ok := delta[\"content\"].(string); ok {{").unwrap();
        writeln!(out, "\t\t\t\t\t\t\tcontent += c").unwrap();
        writeln!(out, "\t\t\t\t\t\t}}").unwrap();
        writeln!(out, "\t\t\t\t\t}}").unwrap();
        writeln!(out, "\t\t\t\t}}").unwrap();
        writeln!(out, "\t\t\t}}").unwrap();
        writeln!(out, "\t\t}}").unwrap();
        writeln!(
            out,
            "\t\tAssertEqual(t, \"stream content\", {:?}, content)",
            expected_content
        )
        .unwrap();
    }
}

