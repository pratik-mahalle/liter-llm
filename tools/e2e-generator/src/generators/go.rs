use std::fmt::Write as FmtWrite;

use anyhow::Result;
use camino::Utf8Path;

use crate::fixture::{Fixture, group_by_category};
use crate::generators::Generator;

pub struct GoGenerator;

impl Generator for GoGenerator {
    fn generate(&self, fixtures: &[Fixture], output_root: &Utf8Path) -> Result<()> {
        let go_dir = output_root.join("go");

        let _ = std::fs::remove_dir_all(&go_dir);
        std::fs::create_dir_all(&go_dir).map_err(|e| anyhow::anyhow!("Failed to create go dir: {e}"))?;

        write_go_mod(&go_dir)?;
        write_helpers_test(&go_dir)?;

        let groups = group_by_category(fixtures);
        for (category, cat_fixtures) in &groups {
            write_test_file(&go_dir, category, cat_fixtures)?;
        }

        // Run gofmt best-effort.
        let _ = std::process::Command::new("gofmt")
            .args(["-w", go_dir.as_str()])
            .status();

        println!("Generated Go e2e tests in {go_dir}");
        Ok(())
    }
}

// ─── File writers ─────────────────────────────────────────────────────────────

fn write_go_mod(dir: &Utf8Path) -> Result<()> {
    // The require directive for the Go wrapper package is included so that
    // consumers can replace it with a local path override during development:
    //   go mod edit -replace github.com/kreuzberg-dev/liter-lm/go=../../../packages/go
    let content = "// Code generated by liter-lm-e2e-generator. DO NOT EDIT.\nmodule literlm_e2e\n\ngo 1.23\n\nrequire github.com/kreuzberg-dev/liter-lm/go v0.0.0\n";
    std::fs::write(dir.join("go.mod"), content).map_err(|e| anyhow::anyhow!("Failed to write go.mod: {e}"))
}

fn write_helpers_test(dir: &Utf8Path) -> Result<()> {
    let content = r#"// Code generated by liter-lm-e2e-generator. DO NOT EDIT.

package literlm_e2e

import (
	"io"
	"net/http"
	"net/http/httptest"
	"strings"
	"testing"
)

// MockRoute describes a single HTTP route the mock server will handle.
type MockRoute struct {
	Path         string
	Method       string
	Status       int
	Body         string
	StreamChunks []string
}

// MockServer wraps an httptest.Server configured with a set of routes.
type MockServer struct {
	Server *httptest.Server
	URL    string
}

// NewMockServer creates and starts a mock server that handles the given routes.
// Routes are matched by path and method; unmatched requests return 404.
func NewMockServer(routes []MockRoute) *MockServer {
	mux := http.NewServeMux()

	for _, route := range routes {
		r := route // capture
		pattern := r.Method + " " + r.Path
		mux.HandleFunc(pattern, func(w http.ResponseWriter, _ *http.Request) {
			if len(r.StreamChunks) > 0 {
				w.Header().Set("Content-Type", "text/event-stream")
				w.Header().Set("Cache-Control", "no-cache")
				w.WriteHeader(r.Status)
				for _, chunk := range r.StreamChunks {
					_, _ = io.WriteString(w, "data: "+chunk+"\n\n")
					if f, ok := w.(http.Flusher); ok {
						f.Flush()
					}
				}
				_, _ = io.WriteString(w, "data: [DONE]\n\n")
				if f, ok := w.(http.Flusher); ok {
					f.Flush()
				}
				return
			}
			w.Header().Set("Content-Type", "application/json")
			w.WriteHeader(r.Status)
			_, _ = io.WriteString(w, r.Body)
		})
	}

	srv := httptest.NewServer(mux)
	return &MockServer{Server: srv, URL: srv.URL}
}

// Close shuts down the mock server.
func (m *MockServer) Close() {
	m.Server.Close()
}

// ReadSSEChunks reads all SSE data lines from a response body.
// It returns the data payloads, excluding the terminal "[DONE]" sentinel.
func ReadSSEChunks(body io.Reader) ([]string, error) {
	data, err := io.ReadAll(body)
	if err != nil {
		return nil, err
	}

	var chunks []string
	for _, line := range strings.Split(string(data), "\n") {
		line = strings.TrimSpace(line)
		if strings.HasPrefix(line, "data: ") {
			payload := strings.TrimPrefix(line, "data: ")
			if payload != "[DONE]" {
				chunks = append(chunks, payload)
			}
		}
	}
	return chunks, nil
}

// AssertEqual fails the test if got != want.
func AssertEqual(t *testing.T, label string, want, got interface{}) {
	t.Helper()
	if want != got {
		t.Errorf("%s: expected %v, got %v", label, want, got)
	}
}

// AssertTrue fails the test if cond is false.
func AssertTrue(t *testing.T, label string, cond bool) {
	t.Helper()
	if !cond {
		t.Errorf("%s: expected true, got false", label)
	}
}
"#;
    std::fs::write(dir.join("helpers_test.go"), content)
        .map_err(|e| anyhow::anyhow!("Failed to write helpers_test.go: {e}"))
}

