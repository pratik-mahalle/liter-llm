fn emit_ruby_chat_assertions(out: &mut String, fixture: &Fixture) {
    if let Some(choices_arr) = fixture.api.mock_response.body.get("choices").and_then(|v| v.as_array()) {
        let count = choices_arr.len();
        writeln!(out, "    expect(body[\"choices\"].size).to eq({count})").unwrap();

        if let Some(first_choice) = choices_arr.first() {
            if let Some(content) = first_choice
                .get("message")
                .and_then(|m| m.get("content"))
                .and_then(|c| c.as_str())
            {
                writeln!(
                    out,
                    "    expect(body.dig(\"choices\", 0, \"message\", \"content\")).to eq({})",
                    ruby_string(content)
                )
                .unwrap();
            }

            if let Some(fr) = first_choice
                .get("finish_reason")
                .and_then(|v| v.as_str())
                .filter(|s| !s.is_empty())
            {
                writeln!(
                    out,
                    "    expect(body.dig(\"choices\", 0, \"finish_reason\")).to eq({})",
                    ruby_string(fr)
                )
                .unwrap();
            }
        }
    }

    if let Some(model) = fixture.api.mock_response.body.get("model").and_then(|v| v.as_str()) {
        writeln!(out, "    expect(body[\"model\"]).to eq({})", ruby_string(model)).unwrap();
    }

    if let Some(usage) = fixture.api.mock_response.body.get("usage").filter(|v| !v.is_null()) {
        writeln!(out, "    expect(body[\"usage\"]).not_to be_nil").unwrap();
        if let Some(total) = usage.get("total_tokens").and_then(|v| v.as_u64()) {
            writeln!(out, "    expect(body.dig(\"usage\", \"total_tokens\")).to eq({total})").unwrap();
        }
    }
}

// ─── Helpers ──────────────────────────────────────────────────────────────────

fn endpoint_for_method(method: &str) -> &'static str {
    match method {
        "chat" | "chat_stream" => "/chat/completions",
        "embed" => "/embeddings",
        "list_models" => "/models",
        _ => "/chat/completions",
    }
}

/// Produce a Ruby string literal. Uses single quotes when safe, double quotes otherwise.
fn ruby_string(s: &str) -> String {
    if s.contains('\'') || s.contains('\\') {
        // Use double quotes with minimal escaping.
        let escaped = s.replace('\\', "\\\\").replace('"', "\\\"");
        format!("\"{escaped}\"")
    } else {
        format!("'{s}'")
    }
}

fn sanitize_name(name: &str) -> String {
    name.chars()
        .map(|c| if c.is_alphanumeric() || c == '_' { c } else { '_' })
        .collect::<String>()
        .to_lowercase()
}
