use std::fmt::Write as FmtWrite;

use anyhow::Result;
use camino::Utf8Path;

use crate::fixture::{Fixture, group_by_category};
use crate::generators::Generator;

pub struct PythonGenerator;

impl Generator for PythonGenerator {
    fn generate(&self, fixtures: &[Fixture], output_root: &Utf8Path) -> Result<()> {
        let py_dir = output_root.join("python");

        // Clean stale output before regenerating.
        let _ = std::fs::remove_dir_all(&py_dir);

        std::fs::create_dir_all(py_dir.join("tests"))
            .map_err(|e| anyhow::anyhow!("Failed to create python/tests dir: {e}"))?;

        write_pyproject_toml(&py_dir)?;
        write_tests_init(&py_dir)?;
        write_conftest(&py_dir)?;
        write_helpers(&py_dir)?;

        let groups = group_by_category(fixtures);
        for (category, cat_fixtures) in &groups {
            write_test_file(&py_dir, category, cat_fixtures)?;
        }

        // Run ruff check --fix + ruff format if available (best-effort; ignore errors).
        let _ = std::process::Command::new("uv")
            .args(["run", "ruff", "check", "--fix", "--quiet", "tests/"])
            .current_dir(&py_dir)
            .status();
        let _ = std::process::Command::new("uv")
            .args(["run", "ruff", "format", "--quiet", "tests/"])
            .current_dir(&py_dir)
            .status();

        println!("Generated Python e2e tests in {py_dir}");
        Ok(())
    }
}

