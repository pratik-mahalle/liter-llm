// ─── Chat assertion helpers ────────────────────────────────────────────────────

fn emit_chat_assertions(out: &mut String, fixture: &Fixture) {
    writeln!(out).unwrap();

    // choices count from mock response body.
    if let Some(choices_arr) = fixture.api.mock_response.body.get("choices").and_then(|v| v.as_array()) {
        let count = choices_arr.len();
        writeln!(
            out,
            "    assert len(response.choices) == {count}, f\"Expected {count} choice(s), got {{len(response.choices)}}\""
        )
        .unwrap();

        if let Some(first_choice) = choices_arr.first() {
            // message content
            if let Some(content) = first_choice
                .get("message")
                .and_then(|m| m.get("content"))
                .and_then(|c| c.as_str())
            {
                writeln!(
                    out,
                    "    assert response.choices[0].message.content == {content:?}, \"message content mismatch\""
                )
                .unwrap();
            }

            // finish_reason
            if let Some(fr) = first_choice
                .get("finish_reason")
                .and_then(|v| v.as_str())
                .filter(|s| !s.is_empty())
            {
                writeln!(
                    out,
                    "    assert response.choices[0].finish_reason == {fr:?}, \"finish_reason mismatch\""
                )
                .unwrap();
            }

            // tool_calls presence
            if first_choice
                .get("message")
                .and_then(|m| m.get("tool_calls"))
                .and_then(|t| t.as_array())
                .is_some_and(|arr| !arr.is_empty())
            {
                writeln!(
                    out,
                    "    assert response.choices[0].message.tool_calls, \"Expected tool_calls\""
                )
                .unwrap();

                if let Some(fn_name) = first_choice
                    .get("message")
                    .and_then(|m| m.get("tool_calls"))
                    .and_then(|t| t.as_array())
                    .and_then(|arr| arr.first())
                    .and_then(|tc| tc.get("function"))
                    .and_then(|f| f.get("name"))
                    .and_then(|n| n.as_str())
                {
                    writeln!(
                        out,
                        "    assert response.choices[0].message.tool_calls[0].function.name == {fn_name:?}, \"tool call function name mismatch\""
                    )
                    .unwrap();
                }
            }
        }
    }

    // usage
    if let Some(usage) = fixture.api.mock_response.body.get("usage").filter(|v| !v.is_null()) {
        writeln!(out, "    assert response.usage is not None, \"Expected usage object\"").unwrap();
        if let Some(total) = usage.get("total_tokens").and_then(|v| v.as_u64()) {
            writeln!(
                out,
                "    assert response.usage.total_tokens == {total}, \"total_tokens mismatch\""
            )
            .unwrap();
        }
    }

    // model
    let model = fixture
        .assertions
        .model
        .as_deref()
        .or_else(|| fixture.api.mock_response.body.get("model").and_then(|v| v.as_str()));
    if let Some(model) = model {
        writeln!(out, "    assert response.model == {model:?}, \"model mismatch\"").unwrap();
    }
}

// ─── Helpers ──────────────────────────────────────────────────────────────────

fn endpoint_for_method(method: &str) -> &'static str {
    match method {
        "chat" | "chat_stream" => "/chat/completions",
        "embed" => "/embeddings",
        "list_models" => "/models",
        "image_generate" => "/images/generations",
        "speech" => "/audio/speech",
        "transcribe" => "/audio/transcriptions",
        "moderate" => "/moderations",
        "rerank" => "/rerank",
        "create_file" | "list_files" => "/files",
        "retrieve_file" | "delete_file" => "/files/file-placeholder",
        "file_content" => "/files/file-placeholder/content",
        "create_batch" | "list_batches" => "/batches",
        "retrieve_batch" | "cancel_batch" => "/batches/batch-placeholder",
        "create_response" => "/responses",
        "retrieve_response" | "cancel_response" => "/responses/resp-placeholder",
        _ => "/chat/completions",
    }
}

fn error_exception_class(status: u16) -> &'static str {
    match status {
        401 => "AuthenticationError",
        429 => "RateLimitedError",
        400 => "BadRequestError",
        404 => "NotFoundError",
        500 => "ServerError",
        502 | 503 => "ServiceUnavailableError",
        _ => "Exception",
    }
}

fn sanitize_name(name: &str) -> String {
    name.chars()
        .map(|c| if c.is_alphanumeric() || c == '_' { c } else { '_' })
        .collect::<String>()
        .to_lowercase()
}
