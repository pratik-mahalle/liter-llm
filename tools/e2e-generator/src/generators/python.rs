fn write_conftest(dir: &Utf8Path) -> Result<()> {
    // Write mock_server.py — importable module with MockRoute, MockServerInfo, handler
    let mock_server_content = r#"# Code generated by liter-lm-e2e-generator. DO NOT EDIT.

from __future__ import annotations

import json
from typing import Any

from aiohttp import web


class MockServerInfo:
    """Holds the URL of a running mock aiohttp server."""

    def __init__(self, url: str) -> None:
        self.url = url


class MockRoute:
    """Configuration for a single mock HTTP route."""

    def __init__(
        self,
        path: str,
        method: str,
        status: int,
        body: Any = None,
        stream_chunks: list[Any] | None = None,
    ) -> None:
        self.path = path
        self.method = method.upper()
        self.status = status
        self.body = body or {}
        self.stream_chunks = stream_chunks or []


def make_handler(route: MockRoute):  # noqa: ANN202
    """Create an aiohttp handler for the given mock route."""
    async def handler(request: web.Request) -> web.Response:
        if route.stream_chunks:
            response = web.StreamResponse(
                status=route.status,
                headers={
                    "Content-Type": "text/event-stream",
                    "Cache-Control": "no-cache",
                },
            )
            await response.prepare(request)
            for chunk in route.stream_chunks:
                data = json.dumps(chunk) if not isinstance(chunk, str) else chunk
                await response.write(f"data: {data}\n\n".encode())
            await response.write(b"data: [DONE]\n\n")
            return response
        body = route.body if isinstance(route.body, str) else json.dumps(route.body)
        return web.Response(
            status=route.status,
            content_type="application/json",
            body=body,
        )

    return handler
"#;
    std::fs::write(dir.join("tests").join("mock_server.py"), mock_server_content)
        .map_err(|e| anyhow::anyhow!("Failed to write tests/mock_server.py: {e}"))?;

    // Write conftest.py — just the pytest fixture, imports from mock_server
    let conftest_content = r#"# Code generated by liter-lm-e2e-generator. DO NOT EDIT.

from __future__ import annotations

from collections.abc import AsyncGenerator

import pytest
from aiohttp import web

from .mock_server import MockRoute, MockServerInfo, make_handler


@pytest.fixture
async def mock_server(request: pytest.FixtureRequest) -> AsyncGenerator[MockServerInfo, None]:
    """Start a mock aiohttp server.

    Parameterize via ``@pytest.mark.parametrize`` or indirect fixture with a
    list of MockRoute instances passed through ``request.param``.
    """
    routes: list[MockRoute] = request.param if hasattr(request, "param") else []

    app = web.Application()
    for route in routes:
        method_lower = route.method.lower()
        router_method = getattr(app.router, f"add_{method_lower}", None)
        if router_method is not None:
            router_method(route.path, make_handler(route))

    runner = web.AppRunner(app)
    await runner.setup()
    site = web.TCPSite(runner, "127.0.0.1", 0)
    await site.start()

    port = site._server.sockets[0].getsockname()[1]  # type: ignore[union-attr]
    url = f"http://127.0.0.1:{port}"

    yield MockServerInfo(url)

    await runner.cleanup()
"#;
    std::fs::write(dir.join("tests").join("conftest.py"), conftest_content)
        .map_err(|e| anyhow::anyhow!("Failed to write tests/conftest.py: {e}"))
}

fn write_helpers(dir: &Utf8Path) -> Result<()> {
    let content = r#"# Code generated by liter-lm-e2e-generator. DO NOT EDIT.

from __future__ import annotations

from typing import Any

import pytest


def assert_choices_count(response: Any, expected: int) -> None:
    """Assert the number of choices in a chat completion response."""
    actual = len(response.choices)
    if actual != expected:
        pytest.fail(f"Expected {expected} choice(s), got {actual}")


def assert_first_choice_content(response: Any, expected: str) -> None:
    """Assert the content of the first choice message."""
    actual = response.choices[0].message.content
    if actual != expected:
        pytest.fail(f"Expected first choice content {expected!r}, got {actual!r}")


def assert_usage(response: Any, field: str, expected: int) -> None:
    """Assert a specific usage field value."""
    usage = response.usage
    if usage is None:
        pytest.fail("Expected usage object but got None")
    actual = getattr(usage, field, None)
    if actual != expected:
        pytest.fail(f"Expected usage.{field} == {expected}, got {actual}")


def assert_model(response: Any, expected: str) -> None:
    """Assert the model identifier in the response."""
    actual = response.model
    if actual != expected:
        pytest.fail(f"Expected model {expected!r}, got {actual!r}")


def assert_error_type(error: Exception, expected: str) -> None:
    """Assert the error class name matches expected."""
    actual = type(error).__name__
    if actual != expected:
        pytest.fail(f"Expected error type {expected!r}, got {actual!r}")


def assert_stream_chunks(chunks: list[Any], min_count: int) -> None:
    """Assert a minimum number of stream chunks were received."""
    actual = len(chunks)
    if actual < min_count:
        pytest.fail(f"Expected at least {min_count} stream chunk(s), got {actual}")
"#;
    std::fs::write(dir.join("tests").join("helpers.py"), content)
        .map_err(|e| anyhow::anyhow!("Failed to write tests/helpers.py: {e}"))
}

// ─── Test file generation ─────────────────────────────────────────────────────

fn write_test_file(dir: &Utf8Path, category: &str, fixtures: &[&Fixture]) -> Result<()> {
    let mut out = String::new();

    writeln!(out, "# Code generated by liter-lm-e2e-generator. DO NOT EDIT.").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "from __future__ import annotations").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "import pytest").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "from .mock_server import MockRoute, MockServerInfo").unwrap();

    // Check if any fixture in this category uses error assertions.
    let has_errors = fixtures
        .iter()
        .any(|f| f.api.mock_response.status >= 400 || !f.assertions.expect_success);

    if has_errors {
        writeln!(out, "from liter_lm import (  # noqa: E402").unwrap();
        writeln!(out, "    LlmClient,").unwrap();
        writeln!(out, "    AuthenticationError,").unwrap();
        writeln!(out, "    RateLimitedError,").unwrap();
        writeln!(out, "    ServerError,").unwrap();
        writeln!(out, "    NotFoundError,").unwrap();
        writeln!(out, "    ServiceUnavailableError,").unwrap();
        writeln!(out, "    BadRequestError,").unwrap();
        writeln!(out, ")").unwrap();
    } else {
        writeln!(out, "from liter_lm import LlmClient  # noqa: E402").unwrap();
    }

    writeln!(out).unwrap();

    for fixture in fixtures {
        if fixture.skip.languages.iter().any(|l| l == "python") {
            continue;
        }
        write_test_fn(&mut out, fixture);
    }

    let out = out.trim_end().to_string() + "\n";

    let filename = format!("test_{}.py", sanitize_name(category));
    let path = dir.join("tests").join(&filename);
    std::fs::write(&path, &out).map_err(|e| anyhow::anyhow!("Failed to write {filename}: {e}"))
}

fn write_test_fn(out: &mut String, fixture: &Fixture) {
    let fn_name = sanitize_name(&fixture.id);
    let method = fixture.api.method.as_str();
    let endpoint = endpoint_for_method(method);
    let http_method = match method {
        "list_models" | "retrieve_file" | "list_files" | "file_content" | "retrieve_batch" | "list_batches"
        | "retrieve_response" => "GET",
        "delete_file" | "cancel_batch" | "cancel_response" => "POST",
        _ => "POST",
    };
    let status = fixture.api.mock_response.status;

    writeln!(out).unwrap();
    writeln!(out, "@pytest.mark.asyncio").unwrap();

    // Build the mock_server parametrize decorator.
    let body_json = serde_json::to_string(&fixture.api.mock_response.body).unwrap_or_default();

    if fixture.api.mock_response.stream_chunks.is_empty() {
        writeln!(out, "@pytest.mark.parametrize(\"mock_server\", [[").unwrap();
        writeln!(
            out,
            "    MockRoute({endpoint:?}, {http_method:?}, {status}, {body_json:?}),"
        )
        .unwrap();
        writeln!(out, "]], indirect=True)").unwrap();
    } else {
        writeln!(out, "@pytest.mark.parametrize(\"mock_server\", [[").unwrap();
        writeln!(out, "    MockRoute(").unwrap();
        writeln!(out, "        {endpoint:?},").unwrap();
        writeln!(out, "        {http_method:?},").unwrap();
        writeln!(out, "        {status},").unwrap();
        writeln!(out, "        stream_chunks=[").unwrap();
        for chunk in &fixture.api.mock_response.stream_chunks {
            let chunk_json = serde_json::to_string(chunk).unwrap_or_default();
            writeln!(out, "            {chunk_json:?},").unwrap();
        }
        writeln!(out, "        ],").unwrap();
        writeln!(out, "    ),").unwrap();
        writeln!(out, "]], indirect=True)").unwrap();
    }

    writeln!(out, "async def test_{fn_name}(mock_server: MockServerInfo) -> None:").unwrap();
    writeln!(out, "    \"\"\"{}\"\"\"", fixture.description).unwrap();

    match method {
        "chat" => emit_chat_test(out, fixture),
        "chat_stream" => emit_stream_test(out, fixture),
        "embed" => emit_embed_test(out, fixture),
        "list_models" => emit_list_models_test(out, fixture),
        other => {
            writeln!(out, "    # TODO: unknown method {other:?}").unwrap();
        }
    }

    writeln!(out).unwrap();
}

