fn write_helpers(dir: &Utf8Path) -> Result<()> {
    // Identical mock server pattern to the TypeScript binding but importing from liter-lm-wasm.
    let content = r#"// Code generated by liter-lm-e2e-generator. DO NOT EDIT.
import http from "node:http";
import type { AddressInfo } from "node:net";

export interface MockRoute {
  path: string;
  method: string;
  status: number;
  body: string;
  streamChunks: string[];
}

export interface MockServer {
  url: string;
  close: () => void;
}

export async function startMockServer(routes: MockRoute[]): Promise<MockServer> {
  const server = http.createServer((req, res) => {
    const route = routes.find(
      (r) => req.url === r.path && req.method === r.method,
    );
    if (!route) {
      res.writeHead(404);
      res.end(`No mock route for ${req.method} ${req.url}`);
      return;
    }
    if (route.streamChunks.length > 0) {
      res.writeHead(route.status, {
        "Content-Type": "text/event-stream",
        "Cache-Control": "no-cache",
        Connection: "keep-alive",
      });
      for (const chunk of route.streamChunks) {
        res.write(`data: ${chunk}\n\n`);
      }
      res.write("data: [DONE]\n\n");
      res.end();
    } else {
      res.writeHead(route.status, { "Content-Type": "application/json" });
      res.end(route.body);
    }
  });

  await new Promise<void>((resolve) => {
    server.listen(0, "127.0.0.1", () => resolve());
  });

  const addr = server.address() as AddressInfo;
  const url = `http://127.0.0.1:${addr.port}`;

  return {
    url,
    close: () => server.close(),
  };
}

export function assertChoicesCount(response: unknown, expected: number): void {
  const choices = (response as { choices?: unknown[] }).choices;
  if (!Array.isArray(choices)) {
    throw new Error(`Expected choices array, got ${typeof choices}`);
  }
  if (choices.length !== expected) {
    throw new Error(`Expected ${expected} choices, got ${choices.length}`);
  }
}

export function assertFirstChoiceContent(
  response: unknown,
  expected: string,
): void {
  const choices = (response as { choices?: unknown[] }).choices;
  if (!Array.isArray(choices) || choices.length === 0) {
    throw new Error("No choices in response");
  }
  const content = (
    choices[0] as { message?: { content?: string } }
  ).message?.content;
  if (content !== expected) {
    throw new Error(
      `Expected content ${JSON.stringify(expected)}, got ${JSON.stringify(content)}`,
    );
  }
}
"#;
    std::fs::write(dir.join("tests").join("helpers.ts"), content)
        .map_err(|e| anyhow::anyhow!("Failed to write tests/helpers.ts: {e}"))
}

// ─── Spec file generation ─────────────────────────────────────────────────────

fn write_spec_file(dir: &Utf8Path, category: &str, fixtures: &[&Fixture]) -> Result<()> {
    let mut out = String::new();

    writeln!(out, "// Code generated by liter-lm-e2e-generator. DO NOT EDIT.").unwrap();
    writeln!(out).unwrap();
    writeln!(
        out,
        "import {{ describe, it, expect, beforeAll, afterAll }} from \"vitest\";"
    )
    .unwrap();
    writeln!(out, "import {{ startMockServer }} from \"./helpers.js\";").unwrap();
    writeln!(out, "import type {{ MockServer }} from \"./helpers.js\";").unwrap();
    writeln!(out).unwrap();
    // Import the WASM client — the actual type is resolved at runtime.
    writeln!(out, "// Note: liter-lm-wasm must be built before running these tests.").unwrap();
    writeln!(
        out,
        "// Run `wasm-pack build --target nodejs` in crates/liter-lm-wasm first."
    )
    .unwrap();
    writeln!(out, "import {{ LlmClient }} from \"liter-lm-wasm\";").unwrap();
    writeln!(out).unwrap();

    for fixture in fixtures {
        if fixture.skip.languages.iter().any(|l| l == "wasm") {
            continue;
        }
        write_describe_block(&mut out, fixture);
    }

    let out = out.trim_end().to_string() + "\n";

    let filename = format!("{}.spec.ts", sanitize_name(category));
    let path = dir.join("tests").join(&filename);
    std::fs::write(&path, &out).map_err(|e| anyhow::anyhow!("Failed to write {filename}: {e}"))
}

fn write_describe_block(out: &mut String, fixture: &Fixture) {
    let method = fixture.api.method.as_str();
    let endpoint = endpoint_for_method(method);
    let http_method = if method == "list_models" { "GET" } else { "POST" };
    let status = fixture.api.mock_response.status;
    let is_error = status >= 400 || !fixture.assertions.expect_success;

    writeln!(out).unwrap();
    writeln!(out, "describe({:?}, () => {{", fixture.description).unwrap();
    writeln!(out, "  let server: MockServer;").unwrap();
    writeln!(out).unwrap();

    // beforeAll: start mock server.
    writeln!(out, "  beforeAll(async () => {{").unwrap();

    let body_json = serde_json::to_string(&fixture.api.mock_response.body).unwrap_or_default();

    if fixture.api.mock_response.stream_chunks.is_empty() {
        writeln!(
            out,
            "    server = await startMockServer([{{ path: {endpoint:?}, method: {http_method:?}, status: {status}, body: {body_json:?}, streamChunks: [] }}]);"
        ).unwrap();
    } else {
        writeln!(out, "    server = await startMockServer([{{").unwrap();
        writeln!(out, "      path: {endpoint:?},").unwrap();
        writeln!(out, "      method: {http_method:?},").unwrap();
        writeln!(out, "      status: {status},").unwrap();
        writeln!(out, "      body: {body_json:?},").unwrap();
        writeln!(out, "      streamChunks: [").unwrap();
        for chunk in &fixture.api.mock_response.stream_chunks {
            let chunk_json = serde_json::to_string(chunk).unwrap_or_default();
            writeln!(out, "        {chunk_json:?},").unwrap();
        }
        writeln!(out, "      ],").unwrap();
        writeln!(out, "    }}]);").unwrap();
    }

    writeln!(out, "  }});").unwrap();
    writeln!(out).unwrap();

    writeln!(out, "  afterAll(() => {{").unwrap();
    writeln!(out, "    server.close();").unwrap();
    writeln!(out, "  }});").unwrap();
    writeln!(out).unwrap();

    writeln!(out, "  it({:?}, async () => {{", fixture.description).unwrap();
    writeln!(
        out,
        "    const client = new LlmClient({{ apiKey: \"test-key\", baseUrl: server.url, maxRetries: 0 }});"
    )
    .unwrap();
    writeln!(out).unwrap();

    let req_json = serde_json::to_string(&fixture.api.request).unwrap_or_default();

    match method {
        "chat" => {
            writeln!(out, "    const req = JSON.parse({req_json:?});").unwrap();
            if is_error {
                writeln!(out, "    await expect(client.chat(req)).rejects.toThrow();").unwrap();
            } else {
                writeln!(out, "    const response = await client.chat(req);").unwrap();
                emit_wasm_chat_assertions(out, fixture);
            }
        }
        "chat_stream" => {
            writeln!(out, "    const req = JSON.parse({req_json:?});").unwrap();
            writeln!(out, "    const chunks: unknown[] = [];").unwrap();
            writeln!(out, "    for await (const chunk of client.chatStream(req)) {{").unwrap();
            writeln!(out, "      chunks.push(chunk);").unwrap();
            writeln!(out, "    }}").unwrap();
            writeln!(out).unwrap();

            let meaningful: usize = fixture
                .api
                .mock_response
                .stream_chunks
                .iter()
                .filter(|c| {
                    c.get("choices")
                        .and_then(|ch| ch.as_array())
                        .and_then(|arr| arr.first())
                        .and_then(|ch| ch.get("delta"))
                        .and_then(|d| d.get("content"))
                        .and_then(|v| v.as_str())
                        .is_some_and(|s| !s.is_empty())
                })
                .count();
            let min_chunks = meaningful.max(1);

            writeln!(out, "    expect(chunks.length).toBeGreaterThanOrEqual({min_chunks});").unwrap();
        }
        "embed" => {
            writeln!(out, "    const req = JSON.parse({req_json:?});").unwrap();
            writeln!(out, "    const response = await client.embed(req);").unwrap();
            emit_wasm_embed_assertions(out, fixture);
        }
        "list_models" => {
            writeln!(out, "    const response = await client.listModels();").unwrap();
            emit_wasm_list_models_assertions(out, fixture);
        }
        other => {
            writeln!(out, "    // TODO: unknown method {other:?}").unwrap();
        }
    }

    writeln!(out, "  }});").unwrap();
    writeln!(out, "}});").unwrap();
}

fn emit_wasm_chat_assertions(out: &mut String, fixture: &Fixture) {
    if let Some(choices_arr) = fixture.api.mock_response.body.get("choices").and_then(|v| v.as_array()) {
        let count = choices_arr.len();
        writeln!(
            out,
            "    expect((response as {{ choices: unknown[] }}).choices).toHaveLength({count});"
        )
        .unwrap();

        if let Some(first_choice) = choices_arr.first()
            && let Some(content) = first_choice
                .get("message")
                .and_then(|m| m.get("content"))
                .and_then(|c| c.as_str())
        {
            writeln!(
                    out,
                    "    expect((response as {{ choices: {{ message: {{ content: string }} }}[] }}).choices[0].message.content).toBe({content:?});"
                ).unwrap();
        }
    }

    let model = fixture
        .assertions
        .model
        .as_deref()
        .or_else(|| fixture.api.mock_response.body.get("model").and_then(|v| v.as_str()));
    if let Some(model) = model {
        writeln!(
            out,
            "    expect((response as {{ model: string }}).model).toBe({model:?});"
        )
        .unwrap();
    }
}

fn emit_wasm_embed_assertions(out: &mut String, fixture: &Fixture) {
    if let Some(data_arr) = fixture.api.mock_response.body.get("data").and_then(|v| v.as_array()) {
        let count = data_arr.len();
        writeln!(
            out,
            "    expect((response as {{ data: unknown[] }}).data).toHaveLength({count});"
        )
        .unwrap();
    }
}

fn emit_wasm_list_models_assertions(out: &mut String, fixture: &Fixture) {
    if let Some(data_arr) = fixture.api.mock_response.body.get("data").and_then(|v| v.as_array()) {
        let count = data_arr.len();
        writeln!(
            out,
            "    expect((response as {{ data: unknown[] }}).data).toHaveLength({count});"
        )
        .unwrap();
    }
}

// ─── Helpers ──────────────────────────────────────────────────────────────────

fn endpoint_for_method(method: &str) -> &'static str {
    match method {
        "chat" | "chat_stream" => "/chat/completions",
        "embed" => "/embeddings",
        "list_models" => "/models",
        _ => "/chat/completions",
    }
}

fn sanitize_name(name: &str) -> String {
    name.chars()
        .map(|c| if c.is_alphanumeric() || c == '_' { c } else { '_' })
        .collect::<String>()
        .to_lowercase()
}
