/// New-category categories that get specialized test emitters.
const SPECIALIZED_CATEGORIES: &[&str] = &["cache", "budget", "hooks", "custom_provider"];

fn write_spec_file(dir: &Utf8Path, category: &str, fixtures: &[&Fixture]) -> Result<()> {
    // Parity fixtures are documentation-only; skip test generation entirely.
    if category == "parity" {
        return Ok(());
    }

    let is_specialized = SPECIALIZED_CATEGORIES.contains(&category);

    let mut out = String::new();

    writeln!(out, "// Code generated by liter-llm-e2e-generator. DO NOT EDIT.").unwrap();
    writeln!(out).unwrap();
    writeln!(
        out,
        "import {{ describe, it, expect, beforeAll, afterAll }} from \"vitest\";"
    )
    .unwrap();
    writeln!(out, "import {{ startMockServer }} from \"./helpers.js\";").unwrap();
    writeln!(out, "import type {{ MockServer }} from \"./helpers.js\";").unwrap();
    writeln!(out).unwrap();
    // Import the WASM client — the actual type is resolved at runtime.
    writeln!(out, "// Note: liter-llm-wasm must be built before running these tests.").unwrap();
    writeln!(
        out,
        "// Run `wasm-pack build --target nodejs` in crates/liter-llm-wasm first."
    )
    .unwrap();
    writeln!(out, "import {{ LlmClient }} from \"liter-llm-wasm\";").unwrap();
    writeln!(out).unwrap();

    let active_fixtures: Vec<_> = fixtures
        .iter()
        .filter(|f| !f.skip.languages.iter().any(|l| l == "wasm"))
        .filter(|f| is_specialized || f.api.method != "chat_stream")
        .collect();

    // Skip writing the file entirely if no fixtures remain after filtering.
    if active_fixtures.is_empty() {
        return Ok(());
    }

    if is_specialized {
        // Wrap specialized tests in a single describe block.
        writeln!(out, "describe({category:?}, () => {{").unwrap();
        for fixture in &active_fixtures {
            write_specialized_wasm_test(&mut out, fixture, category);
        }
        writeln!(out, "}});").unwrap();
    } else {
        for fixture in &active_fixtures {
            write_describe_block(&mut out, fixture);
        }
    }

    let out = out.trim_end().to_string() + "\n";

    let filename = format!("{}.spec.ts", sanitize_name(category));
    let path = dir.join("tests").join(&filename);
    std::fs::write(&path, &out).map_err(|e| anyhow::anyhow!("Failed to write {filename}: {e}"))
}

fn write_describe_block(out: &mut String, fixture: &Fixture) {
    let method = fixture.api.method.as_str();
    let endpoint = endpoint_for_method(method);
    let http_method = match method {
        "list_models" | "retrieve_file" | "list_files" | "file_content" | "retrieve_batch" | "list_batches"
        | "retrieve_response" => "GET",
        "delete_file" | "cancel_batch" | "cancel_response" => "POST",
        _ => "POST",
    };
    let status = fixture.api.mock_response.status;
    let is_error = status >= 400 || !fixture.assertions.expect_success;

    writeln!(out).unwrap();
    writeln!(out, "describe({:?}, () => {{", fixture.description).unwrap();
    writeln!(out, "  let server: MockServer;").unwrap();
    writeln!(out).unwrap();

    // beforeAll: start mock server.
    writeln!(out, "  beforeAll(async () => {{").unwrap();

    let body_json = serde_json::to_string(&fixture.api.mock_response.body).unwrap_or_default();

    if fixture.api.mock_response.stream_chunks.is_empty() {
        writeln!(
            out,
            "    server = await startMockServer([{{ path: {endpoint:?}, method: {http_method:?}, status: {status}, body: {body_json:?}, streamChunks: [] }}]);"
        ).unwrap();
    } else {
        writeln!(out, "    server = await startMockServer([{{").unwrap();
        writeln!(out, "      path: {endpoint:?},").unwrap();
        writeln!(out, "      method: {http_method:?},").unwrap();
        writeln!(out, "      status: {status},").unwrap();
        writeln!(out, "      body: {body_json:?},").unwrap();
        writeln!(out, "      streamChunks: [").unwrap();
        for chunk in &fixture.api.mock_response.stream_chunks {
            let chunk_json = serde_json::to_string(chunk).unwrap_or_default();
            writeln!(out, "        {chunk_json:?},").unwrap();
        }
        writeln!(out, "      ],").unwrap();
        writeln!(out, "    }}]);").unwrap();
    }

    writeln!(out, "  }});").unwrap();
    writeln!(out).unwrap();

    writeln!(out, "  afterAll(() => {{").unwrap();
    writeln!(out, "    server.close();").unwrap();
    writeln!(out, "  }});").unwrap();
    writeln!(out).unwrap();

    writeln!(out, "  it({:?}, async () => {{", fixture.description).unwrap();
    writeln!(
        out,
        "    const client = new LlmClient({{ apiKey: \"test-key\", baseUrl: server.url, maxRetries: 0 }});"
    )
    .unwrap();
    writeln!(out).unwrap();

    let req_json = serde_json::to_string(&fixture.api.request).unwrap_or_default();

    match method {
        "chat" => {
            writeln!(out, "    const req = JSON.parse({req_json:?});").unwrap();
            if is_error {
                writeln!(out, "    await expect(client.chat(req)).rejects.toThrow();").unwrap();
            } else {
                writeln!(out, "    const response = await client.chat(req);").unwrap();
                emit_wasm_chat_assertions(out, fixture);
            }
        }
        "chat_stream" => {
            writeln!(out, "    const req = JSON.parse({req_json:?});").unwrap();
            if is_error {
                writeln!(out, "    let threw = false;").unwrap();
                writeln!(out, "    try {{").unwrap();
                writeln!(
                    out,
                    "      for await (const _chunk of client.chatStream(req)) {{ /* drain */ }}"
                )
                .unwrap();
                writeln!(out, "    }} catch (_e) {{").unwrap();
                writeln!(out, "      threw = true;").unwrap();
                writeln!(out, "    }}").unwrap();
                writeln!(out, "    expect(threw).toBe(true);").unwrap();
            } else {
                writeln!(out, "    const chunks: unknown[] = [];").unwrap();
                writeln!(out, "    for await (const chunk of client.chatStream(req)) {{").unwrap();
                writeln!(out, "      chunks.push(chunk);").unwrap();
                writeln!(out, "    }}").unwrap();
                writeln!(out).unwrap();

                if fixture.api.mock_response.stream_chunks.is_empty() {
                    writeln!(out, "    expect(chunks.length).toBe(0);").unwrap();
                } else {
                    let meaningful: usize = fixture
                        .api
                        .mock_response
                        .stream_chunks
                        .iter()
                        .filter(|c| {
                            c.get("choices")
                                .and_then(|ch| ch.as_array())
                                .and_then(|arr| arr.first())
                                .and_then(|ch| ch.get("delta"))
                                .and_then(|d| d.get("content"))
                                .and_then(|v| v.as_str())
                                .is_some_and(|s| !s.is_empty())
                        })
                        .count();
                    let min_chunks = meaningful.max(1);

                    writeln!(out, "    expect(chunks.length).toBeGreaterThanOrEqual({min_chunks});").unwrap();
                }
            }
        }
        "embed" => {
            writeln!(out, "    const req = JSON.parse({req_json:?});").unwrap();
            if is_error {
                writeln!(out, "    await expect(client.embed(req)).rejects.toThrow();").unwrap();
            } else {
                writeln!(out, "    const response = await client.embed(req);").unwrap();
                emit_wasm_embed_assertions(out, fixture);
            }
        }
        "list_models" => {
            if is_error {
                writeln!(out, "    await expect(client.listModels()).rejects.toThrow();").unwrap();
            } else {
                writeln!(out, "    const response = await client.listModels();").unwrap();
                emit_wasm_list_models_assertions(out, fixture);
            }
        }
        other => {
            writeln!(out, "    // TODO: unknown method {other:?}").unwrap();
        }
    }

    writeln!(out, "  }});").unwrap();
    writeln!(out, "}});").unwrap();
}

fn emit_wasm_chat_assertions(out: &mut String, fixture: &Fixture) {
    if let Some(choices_arr) = fixture.api.mock_response.body.get("choices").and_then(|v| v.as_array()) {
        let count = choices_arr.len();
        writeln!(
            out,
            "    expect((response as {{ choices: unknown[] }}).choices).toHaveLength({count});"
        )
        .unwrap();

        if let Some(first_choice) = choices_arr.first()
            && let Some(content) = first_choice
                .get("message")
                .and_then(|m| m.get("content"))
                .and_then(|c| c.as_str())
        {
            writeln!(
                    out,
                    "    expect((response as {{ choices: {{ message: {{ content: string }} }}[] }}).choices[0].message.content).toBe({content:?});"
                ).unwrap();
        }
    }

    let model = fixture
        .assertions
        .model
        .as_deref()
        .or_else(|| fixture.api.mock_response.body.get("model").and_then(|v| v.as_str()));
    if let Some(model) = model {
        writeln!(
            out,
            "    expect((response as {{ model: string }}).model).toBe({model:?});"
        )
        .unwrap();
    }
}

