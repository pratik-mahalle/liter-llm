use std::fmt::Write as FmtWrite;

use anyhow::Result;
use camino::Utf8Path;

use crate::fixture::{Fixture, group_by_category};
use crate::generators::Generator;

pub struct WasmGenerator;

impl Generator for WasmGenerator {
    fn generate(&self, fixtures: &[Fixture], output_root: &Utf8Path) -> Result<()> {
        let wasm_dir = output_root.join("wasm");

        // Clean stale output before regenerating.
        let _ = std::fs::remove_dir_all(&wasm_dir);

        std::fs::create_dir_all(wasm_dir.join("tests"))
            .map_err(|e| anyhow::anyhow!("Failed to create wasm/tests dir: {e}"))?;

        write_package_json(&wasm_dir)?;
        write_vitest_config(&wasm_dir)?;
        write_helpers(&wasm_dir)?;

        let groups = group_by_category(fixtures);
        for (category, cat_fixtures) in &groups {
            write_spec_file(&wasm_dir, category, cat_fixtures)?;
        }

        // Run biome formatter if available (best-effort; ignore errors).
        let _ = std::process::Command::new("biome")
            .args(["format", "--write"])
            .arg(wasm_dir.as_str())
            .status();

        println!("Generated WASM e2e tests in {wasm_dir}");
        Ok(())
    }
}

