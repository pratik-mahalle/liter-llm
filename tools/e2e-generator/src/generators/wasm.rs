fn write_package_json(dir: &Utf8Path) -> Result<()> {
    let content = r#"// Code generated by liter-lm-e2e-generator. DO NOT EDIT.
{
  "name": "liter-lm-wasm-e2e-tests",
  "private": true,
  "type": "module",
  "scripts": {
    "test": "vitest run"
  },
  "devDependencies": {
    "vitest": "^3.0.0"
  },
  "dependencies": {
    "liter-lm-wasm": "../../packages/wasm"
  }
}
"#;
    std::fs::write(dir.join("package.json"), content).map_err(|e| anyhow::anyhow!("Failed to write package.json: {e}"))
}

