use crate::fixture::{Fixture, group_by_category};
use crate::generators::Generator;

pub struct CSharpGenerator;

impl Generator for CSharpGenerator {
    fn generate(&self, fixtures: &[Fixture], output_root: &Utf8Path) -> Result<()> {
        let cs_dir = output_root.join("csharp");

        // Clean stale output before regenerating.
        let _ = std::fs::remove_dir_all(&cs_dir);

        std::fs::create_dir_all(&cs_dir).map_err(|e| anyhow::anyhow!("Failed to create csharp dir: {e}"))?;

        write_csproj(&cs_dir)?;
        write_helpers(&cs_dir)?;

        let groups = group_by_category(fixtures);
        for (category, cat_fixtures) in &groups {
            write_test_file(&cs_dir, category, cat_fixtures)?;
        }

        // Run dotnet format if available (best-effort; ignore errors).
        let _ = std::process::Command::new("dotnet")
            .args(["format", "LiterLmE2E.csproj"])
            .current_dir(&cs_dir)
            .status();

        println!("Generated C# e2e tests in {cs_dir}");
        Ok(())
    }
}

