fn endpoint_for_method(method: &str) -> &'static str {
    match method {
        "chat" | "chat_stream" => "/chat/completions",
        "embed" => "/embeddings",
        "list_models" => "/models",
        "image_generate" => "/images/generations",
        "speech" => "/audio/speech",
        "transcribe" => "/audio/transcriptions",
        "moderate" => "/moderations",
        "rerank" => "/rerank",
        "create_file" | "list_files" => "/files",
        "retrieve_file" | "delete_file" => "/files/file-placeholder",
        "file_content" => "/files/file-placeholder/content",
        "create_batch" | "list_batches" => "/batches",
        "retrieve_batch" | "cancel_batch" => "/batches/batch-placeholder",
        "create_response" => "/responses",
        "retrieve_response" | "cancel_response" => "/responses/resp-placeholder",
        _ => "/chat/completions",
    }
}

/// Produce a Java string literal with proper escaping.
fn java_string(s: &str) -> String {
    let escaped = s
        .replace('\\', "\\\\")
        .replace('"', "\\\"")
        .replace('\n', "\\n")
        .replace('\r', "\\r")
        .replace('\t', "\\t");
    format!("\"{escaped}\"")
}

/// Convert a hyphenated/underscored name to PascalCase.
fn pascal_case(s: &str) -> String {
    s.split(['-', '_', ' '])
        .filter(|p| !p.is_empty())
        .map(|part| {
            let mut chars = part.chars();
            match chars.next() {
                None => String::new(),
                Some(first) => first.to_uppercase().collect::<String>() + chars.as_str(),
            }
        })
        .collect()
}

/// Convert a hyphenated/underscored name to camelCase (for Java test method names).
fn camel_case(s: &str) -> String {
    let pascal = pascal_case(s);
    let mut chars = pascal.chars();
    match chars.next() {
        None => String::new(),
        Some(first) => first.to_lowercase().collect::<String>() + chars.as_str(),
    }
}
