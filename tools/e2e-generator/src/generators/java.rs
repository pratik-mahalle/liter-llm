pub struct JavaGenerator;

const JAVA_PKG_PATH: &str = "src/test/java/dev/kreuzberg/literlm/e2e";

impl Generator for JavaGenerator {
    fn generate(&self, fixtures: &[Fixture], output_root: &Utf8Path) -> Result<()> {
        let java_dir = output_root.join("java");

        let _ = std::fs::remove_dir_all(&java_dir);
        let test_pkg_dir = java_dir.join(JAVA_PKG_PATH);
        std::fs::create_dir_all(&test_pkg_dir)
            .map_err(|e| anyhow::anyhow!("Failed to create java test package dir: {e}"))?;

        write_pom_xml(&java_dir)?;
        write_helpers_java(&test_pkg_dir)?;

        let groups = group_by_category(fixtures);
        for (category, cat_fixtures) in &groups {
            write_test_class(&test_pkg_dir, category, cat_fixtures)?;
        }

        // Run google-java-format best-effort.
        if let Ok(files) = std::fs::read_dir(&test_pkg_dir) {
            let java_files: Vec<_> = files
                .filter_map(|e| e.ok())
                .filter(|e| e.path().extension().is_some_and(|ext| ext == "java"))
                .map(|e| e.path())
                .collect();
            if !java_files.is_empty() {
                let _ = std::process::Command::new("google-java-format")
                    .arg("--replace")
                    .args(&java_files)
                    .status();
            }
        }

        println!("Generated Java e2e tests in {java_dir}");
        Ok(())
    }
}

