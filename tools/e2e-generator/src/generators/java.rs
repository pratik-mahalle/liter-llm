fn write_test_class(pkg_dir: &Utf8Path, category: &str, fixtures: &[&Fixture]) -> Result<()> {
    let class_name = format!("{}Test", pascal_case(category));
    let mut out = String::new();

    writeln!(out, "// Code generated by liter-lm-e2e-generator. DO NOT EDIT.").unwrap();
    writeln!(out, "package dev.kreuzberg.literlm.e2e;").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "import static org.junit.jupiter.api.Assertions.*;").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "import com.fasterxml.jackson.databind.JsonNode;").unwrap();
    writeln!(out, "import java.util.List;").unwrap();
    writeln!(out, "import java.net.http.HttpResponse;").unwrap();
    writeln!(out, "import org.junit.jupiter.api.Test;").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "/** E2E tests for category: {category}. */").unwrap();
    writeln!(out, "class {class_name} {{").unwrap();

    for fixture in fixtures {
        if fixture.skip.languages.iter().any(|l| l == "java") {
            continue;
        }
        write_test_method(&mut out, fixture);
    }

    writeln!(out, "}}").unwrap();

    let out = out.trim_end().to_string() + "\n";
    let filename = format!("{class_name}.java");
    let path = pkg_dir.join(&filename);
    std::fs::write(&path, &out).map_err(|e| anyhow::anyhow!("Failed to write {filename}: {e}"))
}

fn write_test_method(out: &mut String, fixture: &Fixture) {
    let method_name = camel_case(&fixture.id);
    let method = fixture.api.method.as_str();
    let endpoint = endpoint_for_method(method);
    let http_method = match method {
        "list_models" | "retrieve_file" | "list_files" | "file_content" | "retrieve_batch" | "list_batches"
        | "retrieve_response" => "GET",
        "delete_file" | "cancel_batch" | "cancel_response" => "POST",
        _ => "POST",
    };
    let status = fixture.api.mock_response.status;

    writeln!(out).unwrap();
    writeln!(out, "  /** {} */", fixture.description).unwrap();
    writeln!(out, "  @Test").unwrap();
    writeln!(out, "  void {method_name}() throws Exception {{").unwrap();

    let body_json = serde_json::to_string(&fixture.api.mock_response.body).unwrap_or_default();

    // Build mock server.
    writeln!(
        out,
        "    try (Helpers.MockServer server = new Helpers.MockServer(List.of("
    )
    .unwrap();

    if fixture.api.mock_response.stream_chunks.is_empty() {
        writeln!(
            out,
            "        new Helpers.MockRoute({:?}, {:?}, {status}, {})",
            endpoint,
            http_method,
            java_string(&body_json)
        )
        .unwrap();
    } else {
        writeln!(
            out,
            "        new Helpers.MockRoute({:?}, {:?}, {status}, {},",
            endpoint,
            http_method,
            java_string(&body_json)
        )
        .unwrap();
        writeln!(out, "            List.of(").unwrap();
        let last = fixture.api.mock_response.stream_chunks.len() - 1;
        for (i, chunk) in fixture.api.mock_response.stream_chunks.iter().enumerate() {
            let chunk_json = serde_json::to_string(chunk).unwrap_or_default();
            let comma = if i < last { "," } else { "" };
            writeln!(out, "                {}{comma}", java_string(&chunk_json)).unwrap();
        }
        writeln!(out, "            ))").unwrap();
    }

    writeln!(out, "    ))) {{").unwrap();
    writeln!(out).unwrap();

    // Emit the API call.
    match method {
        "chat" => emit_java_chat_test(out, fixture, endpoint),
        "chat_stream" => emit_java_stream_test(out, fixture, endpoint),
        "embed" => emit_java_embed_test(out, fixture, endpoint),
        "list_models" => emit_java_list_models_test(out, fixture, endpoint),
        other => {
            writeln!(out, "      // TODO: unknown method {other:?}").unwrap();
        }
    }

    writeln!(out, "    }}").unwrap();
    writeln!(out, "  }}").unwrap();
}

